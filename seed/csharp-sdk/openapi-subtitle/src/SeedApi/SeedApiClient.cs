    /// <summary>
    /// Returns a paginated list of all plants currently in the store inventory.
    /// </summary>
    /// <example><code>
    /// await client.ListPlantsAsync();
    /// </code></example>
    public WithRawResponseTask<IEnumerable<Plant>> ListPlantsAsync(
        RequestOptions? options = null,
        CancellationToken cancellationToken = default
    )
    {
        return new WithRawResponseTask<IEnumerable<Plant>>(
            ListPlantsAsyncCore(options, cancellationToken)
        );
    }

