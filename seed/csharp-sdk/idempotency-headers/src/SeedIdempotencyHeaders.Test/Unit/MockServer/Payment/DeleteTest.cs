using NUnit.Framework;
using SeedIdempotencyHeaders.Test.Unit.MockServer;

namespace SeedIdempotencyHeaders.Test.Unit.MockServer.Payment;

[TestFixture]
[Parallelizable(ParallelScope.Self)]
public class DeleteTest : BaseMockServerTest
{
    [NUnit.Framework.Test]
    public void MockServerTest()
    {
        Server
            .Given(
                WireMock
                    .RequestBuilders.Request.Create()
                    .WithPath("/payment/paymentId")
                    .UsingDelete()
            )
            .RespondWith(WireMock.ResponseBuilders.Response.Create().WithStatusCode(200));

        Assert.DoesNotThrowAsync(async () => await Client.Payment.DeleteAsync("paymentId"));
    }
}
