import { readFileSync } from "node:fs";
import { resolve } from "node:path";

import { formatEndpointPathForSwift } from "../format-endpoint-path-for-swift.js";

interface EndpointFixtureEntry {
    services: Record<
        string,
        {
            endpoints: Array<{
                fullPath: {
                    head: string;
                    parts: Array<{ pathParameter: string; tail: string }>;
                };
                allPathParameters: Array<{
                    name: { originalName: string; camelCase: { unsafeName: string } };
                    docs: string | undefined;
                }>;
            }>;
        }
    >;
}

interface EndpointFixtures {
    _definitionsHash: string;
    [definitionName: string]: EndpointFixtureEntry | string;
}

// Pre-generated endpoint fixture data, auto-generated by generate-endpoint-fixtures.globalSetup.ts
// before tests load via vitest's globalSetup hook.
const fixturesPath = resolve(__dirname, "endpoint-fixtures.json");
const endpointFixtures: EndpointFixtures = JSON.parse(readFileSync(fixturesPath, "utf-8"));

const testDefinitionNames = Object.keys(endpointFixtures)
    .filter((key) => key !== "_definitionsHash")
    .sort();

describe.each(testDefinitionNames)("formatEndpointPathForSwift - %s", (testDefinitionName) => {
    // This allows us to conveniently review the formatted endpoint paths for every test definition in a single location

    it("correctly formats all endpoint paths for definition", () => {
        const fixture = endpointFixtures[testDefinitionName] as EndpointFixtureEntry;
        const endpointPathsByService = Object.fromEntries(
            Object.entries(fixture.services).map(([serviceName, service]) => {
                return [
                    serviceName,
                    // biome-ignore lint/suspicious/noExplicitAny: allow explicit any
                    service.endpoints.map((endpoint) => formatEndpointPathForSwift(endpoint as any))
                ] as const;
            })
        );
        const fileContents = Object.entries(endpointPathsByService)
            .map(([serviceName, paths]) => ({ serviceName, serviceContent: paths.map((p) => `"${p}"`).join("\n") }))
            .map(({ serviceName, serviceContent }) => `// ${serviceName}\n${serviceContent}`)
            .join("\n\n");
        expect(fileContents).toMatchSnapshot();
    });
});
