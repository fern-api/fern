import { FernIr } from "@fern-fern/ir-sdk";
export function getExampleEndpointCallsForTests({
    endpoint,
    fallbackToAutoGeneratedErrorExamples = true
}: {
    endpoint: FernIr.HttpEndpoint;
    fallbackToAutoGeneratedErrorExamples?: boolean;
}): FernIr.ExampleEndpointCall[] {
    const examples: FernIr.ExampleEndpointCall[] = [];
    // If any of the examples are user provided, we should only include those.
    const hasUserExamples = endpoint.userSpecifiedExamples.length > 0;
    const userExamplesHasErrorExample = endpoint.userSpecifiedExamples.some(
        (e) => e.example?.response.type === "error"
    );

    // For SSE endpoints, prefer autogenerated examples when they have more events
    // (multi-variant coverage) than user-specified examples. The user-specified examples
    // from OpenAPI imports often only contain a single SSE event for the first variant.
    if (hasUserExamples && hasBetterAutoGeneratedSseExamples(endpoint)) {
        examples.push(...endpoint.autogeneratedExamples.map((e) => e.example).filter((e) => e != null));
        return examples;
    }

    if (hasUserExamples) {
        examples.push(...endpoint.userSpecifiedExamples.map((e) => e.example).filter((e) => e != null));
        // if no user examples, add auto generated error examples
        if (!userExamplesHasErrorExample && fallbackToAutoGeneratedErrorExamples) {
            examples.push(
                ...endpoint.autogeneratedExamples
                    .map((e) => e.example)
                    .filter((e) => e != null)
                    .filter((e) => e.response.type === "error")
            );
        }

        return examples;
    }

    // Otherwise we should only include a single one of the generated examples.
    examples.push(...endpoint.autogeneratedExamples.map((e) => e.example).filter((e) => e != null));
    return examples;
}

function getSseEventCount(example: FernIr.ExampleEndpointCall): number {
    if (example.response.type !== "ok") {
        return 0;
    }
    return example.response.value._visit({
        sse: (events) => events.length,
        body: () => 0,
        stream: () => 0,
        _other: () => 0
    });
}

function hasBetterAutoGeneratedSseExamples(endpoint: FernIr.HttpEndpoint): boolean {
    const userExamples = endpoint.userSpecifiedExamples.map((e) => e.example).filter((e) => e != null);
    const autoExamples = endpoint.autogeneratedExamples.map((e) => e.example).filter((e) => e != null);
    const maxUserSseEvents = Math.max(0, ...userExamples.map(getSseEventCount));
    const maxAutoSseEvents = Math.max(0, ...autoExamples.map(getSseEventCount));
    return maxAutoSseEvents > maxUserSseEvents;
}

export function getExampleEndpointCalls(endpoint: FernIr.HttpEndpoint): FernIr.ExampleEndpointCall[] {
    // If any of the examples are user provided, we should only include those.
    if (endpoint.userSpecifiedExamples.length > 0) {
        const examples: FernIr.ExampleEndpointCall[] = [];
        for (const userDefinedExample of endpoint.userSpecifiedExamples) {
            if (userDefinedExample.example != null) {
                examples.push(userDefinedExample.example);
            }
        }
        return examples;
    }
    // Otherwise we should only include a single one of the generated examples.
    for (const autogenerated of endpoint.autogeneratedExamples) {
        if (autogenerated.example != null) {
            return [autogenerated.example];
        }
    }
    return [];
}
