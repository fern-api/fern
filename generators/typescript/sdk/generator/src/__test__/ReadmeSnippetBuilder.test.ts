function createName(name: string): FernIr.Name {
    return {
        originalName: name,
        camelCase: {
            unsafeName: name.charAt(0).toLowerCase() + name.slice(1),
            safeName: name.charAt(0).toLowerCase() + name.slice(1)
        },
        snakeCase: { unsafeName: name.toLowerCase(), safeName: name.toLowerCase() },
        screamingSnakeCase: { unsafeName: name.toUpperCase(), safeName: name.toUpperCase() },
        pascalCase: {
            unsafeName: name.charAt(0).toUpperCase() + name.slice(1),
            safeName: name.charAt(0).toUpperCase() + name.slice(1)
        }
    };
}

function createFernFilepath(parts: string[]): FernIr.FernFilepath {
    return {
        allParts: parts.map((p) => createName(p)),
        packagePath: parts.map((p) => createName(p)),
        // biome-ignore lint/style/noNonNullAssertion: array length already checked
        file: parts.length > 0 ? createName(parts[parts.length - 1]!) : undefined
    };
}

function createEndpoint(id: string, name: string, overrides?: Partial<FernIr.HttpEndpoint>): FernIr.HttpEndpoint {
    return {
        id,
        name: createName(name),
        method: "POST",
        headers: [],
        queryParameters: [],
        pathParameters: [],
        path: { head: `/${name}`, parts: [] },
        fullPath: { head: `/${name}`, parts: [] },
        sdkRequest: undefined,
        requestBody: undefined,
        response: undefined,
        errors: [],
        auth: false,
        idempotent: false,
        pagination: undefined,
        userSpecifiedExamples: [],
        autogeneratedExamples: [],
        baseUrl: undefined,
        availability: undefined,
        docs: undefined,
        v2Examples: undefined,
        transport: undefined,
        ...overrides
        // biome-ignore lint/suspicious/noExplicitAny: IR mock
    } as any;
}

function createService(
    serviceName: string,
    fernFilepath: FernIr.FernFilepath,
    endpoints: FernIr.HttpEndpoint[]
): FernIr.HttpService {
    return {
        name: {
            fernFilepath
        },
        basePath: undefined,
        baseUrl: undefined,
        endpoints,
        headers: [],
        pathParameters: [],
        availability: undefined,
        docs: undefined,
        displayName: undefined
        // biome-ignore lint/suspicious/noExplicitAny: IR mock
    } as any;
}

function createEndpointSnippet(endpointId: string, method: string, snippetCode: string): FernGeneratorExec.Endpoint {
    return {
        id: {
            path: `/${endpointId}`,
            method: method as FernGeneratorExec.EndpointMethod,
            identifierOverride: endpointId
        },
        snippet: FernGeneratorExec.EndpointSnippet.typescript({
            client: snippetCode
        })
        // biome-ignore lint/suspicious/noExplicitAny: mock
    } as any;
}

