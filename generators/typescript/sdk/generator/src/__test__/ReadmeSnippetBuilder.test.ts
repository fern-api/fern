function createMockFileContext(overrides?: {
    services?: Record<string, FernIr.HttpService>;
    readmeConfig?: FernIr.ReadmeConfig;
    subpackages?: Record<string, FernIr.Subpackage>;
    generatePaginatedClients?: boolean;
    packageName?: string;
    namespaceExport?: string;
    authSchemes?: FernIr.AuthScheme[];
}): FileContext {
    const services = overrides?.services ?? {};
    const subpackages = overrides?.subpackages ?? {};
    const readmeConfig = overrides?.readmeConfig;
    const authSchemes = overrides?.authSchemes ?? [];

    return {
        case: new CaseConverter({ generationLanguage: "typescript", keywords: undefined, smartCasing: false }),
        config: {
            generatePaginatedClients: overrides?.generatePaginatedClients ?? false
        },
        ir: {
            services,
            subpackages,
            readmeConfig,
            auth: {
                schemes: authSchemes,
                requirement: "ALL",
                docs: undefined
            }
        },
        npmPackage: overrides?.packageName ? { packageName: overrides.packageName, version: "1.0.0" } : undefined,
        namespaceExport: overrides?.namespaceExport ?? "MySDK",
        sdkClientClass: {
            getReferenceToClientClass: (_opts: { isRoot: boolean }) => ({
                getTypeNode: () => ts.factory.createIdentifier("MySDKClient")
            })
        },
        sdkInstanceReferenceForSnippet: ts.factory.createIdentifier("client"),
        genericAPISdkError: {
            getReferenceToGenericAPISdkError: () => ({
                getEntityName: () => ts.factory.createQualifiedName(ts.factory.createIdentifier("errors"), "MySDKError")
            })
        }
        // biome-ignore lint/suspicious/noExplicitAny: test mock
    } as any;
}

