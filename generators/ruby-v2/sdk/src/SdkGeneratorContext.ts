import { GeneratorNotificationService } from "@fern-api/base-generator";
import { assertNever } from "@fern-api/core-utils";
import { join, RelativeFilePath } from "@fern-api/path-utils";
import { ClassReference, ruby } from "@fern-api/ruby-ast";
import { AbstractRubyGeneratorContext, AsIsFiles, RubyProject } from "@fern-api/ruby-base";
import { FernGeneratorExec } from "@fern-fern/generator-exec-sdk";
import { FernIr } from "@fern-fern/ir-sdk";
import { EndpointGenerator } from "./endpoint/EndpointGenerator.js";
import { RubyGeneratorAgent } from "./RubyGeneratorAgent.js";
import { ReadmeConfigBuilder } from "./readme/ReadmeConfigBuilder.js";
import { EndpointSnippetsGenerator } from "./reference/EndpointSnippetsGenerator.js";
import { SdkCustomConfigSchema } from "./SdkCustomConfig.js";

const ROOT_TYPES_FOLDER = "types";

export class SdkGeneratorContext extends AbstractRubyGeneratorContext<SdkCustomConfigSchema> {
    public readonly project: RubyProject;
    public readonly endpointGenerator: EndpointGenerator;
    public readonly snippetGenerator: EndpointSnippetsGenerator;
    public readonly generatorAgent: RubyGeneratorAgent;

    public constructor(
        public readonly ir: FernIr.IntermediateRepresentation,
        public readonly config: FernGeneratorExec.config.GeneratorConfig,
        public readonly customConfig: SdkCustomConfigSchema,
        public readonly generatorNotificationService: GeneratorNotificationService
    ) {
        super(ir, config, customConfig ?? {}, generatorNotificationService);
        this.project = new RubyProject({ context: this });
        this.endpointGenerator = new EndpointGenerator({ context: this });
        this.snippetGenerator = new EndpointSnippetsGenerator({ context: this });
        this.generatorAgent = new RubyGeneratorAgent({
            logger: this.logger,
            config: this.config,
            readmeConfigBuilder: new ReadmeConfigBuilder(),
            ir
        });
    }

    public getRootFolderPath(): RelativeFilePath {
        return RelativeFilePath.of(["lib", this.getRootFolderName()].join("/"));
    }

    public getLocationForTypeId(typeId: FernIr.TypeId): RelativeFilePath {
        const typeDeclaration = this.getTypeDeclarationOrThrow(typeId);
        return join(
            RelativeFilePath.of("lib"),
            RelativeFilePath.of(this.getRootFolderName()),
            ...this.snakeNames(typeDeclaration).map(RelativeFilePath.of),
            RelativeFilePath.of(this.typesDirName)
        );
    }

    public getClassReferenceForTypeId(typeId: FernIr.TypeId): ruby.ClassReference {
        const typeDeclaration = this.getTypeDeclarationOrThrow(typeId);
        return ruby.classReference({
            modules: this.getModuleNamesForTypeId(typeId),
            name: typeDeclaration.name.name.pascalCase.safeName
        });
    }

    public getFileNameForTypeId(typeId: FernIr.TypeId): string {
        const typeDeclaration = this.getTypeDeclarationOrThrow(typeId);
        return typeDeclaration.name.name.snakeCase.safeName + ".rb";
    }

    public getAllTypeDeclarations(): FernIr.TypeDeclaration[] {
        return Object.values(this.ir.types);
    }

    public getModuleNamesForTypeId(typeId: FernIr.TypeId): string[] {
        const typeDeclaration = this.getTypeDeclarationOrThrow(typeId);
        return [this.getRootModuleName(), ...this.pascalNames(typeDeclaration), this.getTypesModule().name];
    }

    public getModulesForTypeId(typeId: FernIr.TypeId): ruby.Module_[] {
        const modules = this.getModuleNamesForTypeId(typeId);
        return modules.map((name) => ruby.module({ name }));
    }

    public getLocationForSubpackageId(subpackageId: FernIr.SubpackageId): RelativeFilePath {
        const subpackage = this.getSubpackageOrThrow(subpackageId);
        return RelativeFilePath.of(
            [
                "lib",
                this.getRootFolderName(),
                ...subpackage.fernFilepath.allParts.map((path) => path.snakeCase.safeName)
            ].join("/")
        );
    }

    public maybeGetExampleEndpointCall(endpoint: FernIr.HttpEndpoint): FernIr.ExampleEndpointCall | null {
        // Collect all examples (user-specified first, then auto-generated)
        const allExamples: FernIr.ExampleEndpointCall[] = [
            ...endpoint.userSpecifiedExamples
                .map((e) => e.example)
                .filter((e): e is FernIr.ExampleEndpointCall => e != null),
            ...endpoint.autogeneratedExamples
                .map((e) => e.example)
                .filter((e): e is FernIr.ExampleEndpointCall => e != null)
        ];

        // Prefer examples that have a non-empty request body (jsonExample)
        // This ensures we get detailed code snippets with full request bodies
        const exampleWithRequestBody = allExamples.find((ex) => ex.request?.jsonExample != null);

        return exampleWithRequestBody ?? allExamples[0] ?? null;
    }

    public getReturnTypeForEndpoint(httpEndpoint: FernIr.HttpEndpoint): ruby.Type {
        const responseBody = httpEndpoint.response?.body;

        if (responseBody == null) {
            return ruby.Type.void();
        }

        switch (responseBody.type) {
            case "json":
                return this.typeMapper.convert({ reference: responseBody.value.responseBodyType });
            case "fileDownload":
            case "text":
                return ruby.Type.string();
            case "bytes":
                throw new Error("Returning bytes is not supported");
            case "streaming":
            case "streamParameter": {
                const streamingResponse = this.getStreamingResponse(httpEndpoint);
                if (!streamingResponse) {
                    throw new Error(
                        `Unable to parse streaming response for endpoint ${httpEndpoint.name.camelCase.safeName}`
                    );
                }
                return this.getStreamPayload(streamingResponse);
            }
            default:
                assertNever(responseBody);
        }
    }

    public getStreamingResponse(endpoint: FernIr.HttpEndpoint): FernIr.StreamingResponse | undefined {
        const responseBody = endpoint.response?.body;
        if (responseBody == null) {
            return undefined;
        }
        switch (responseBody.type) {
            case "streaming":
                return responseBody.value;
            case "streamParameter":
                return responseBody.streamResponse;
            case "fileDownload":
            case "json":
            case "text":
            case "bytes":
                return undefined;
            default:
                assertNever(responseBody);
        }
    }

    public getStreamPayload(streamingResponse: FernIr.StreamingResponse): ruby.Type {
        switch (streamingResponse.type) {
            case "json":
            case "sse":
                return this.typeMapper.convert({ reference: streamingResponse.payload });
            case "text":
                return ruby.Type.string();
            default:
                assertNever(streamingResponse);
        }
    }

    public getSubpackageOrThrow(subpackageId: FernIr.SubpackageId): FernIr.Subpackage {
        const subpackage = this.ir.subpackages[subpackageId];
        if (subpackage == null) {
            throw new Error(`Subpackage with id ${subpackageId} not found`);
        }
        return subpackage;
    }

    public getHttpServiceOrThrow(serviceId: FernIr.ServiceId): FernIr.HttpService {
        const service = this.ir.services[serviceId];
        if (service == null) {
            throw new Error(`Service with id ${serviceId} not found`);
        }
        return service;
    }

    public getSubpackageForServiceId(serviceId: FernIr.ServiceId): FernIr.Subpackage {
        for (const [_, subpackage] of Object.entries(this.ir.subpackages)) {
            if (subpackage.service === serviceId) {
                return subpackage;
            }
        }
        throw new Error(`No subpackage found for service ${serviceId}`);
    }

    /**
     * Recursively checks if a subpackage has endpoints.
     * @param subpackage - The subpackage to check.
     * @returns True if the subpackage has endpoints, false otherwise.
     *
     * @remarks
     * The `hasEndpointInTree` member may not always be perfectly accurate.
     *
     * This method is a interim workaround that recursively checks all
     * subpackages in order to determine if the subpackage has endpoints.
     *
     * There may be other cases that this method does not handle (GRPC, etc?)
     */
    public subPackageHasEndpoints(subpackage: FernIr.Subpackage): boolean {
        return (
            subpackage.hasEndpointsInTree ||
            subpackage.subpackages.some((pkg) => this.subPackageHasEndpoints(this.getSubpackageOrThrow(pkg)))
        );
    }

    public getRawClientClassReference(): ClassReference {
        return ruby.classReference({
            name: "RawClient",
            modules: [this.getRootModuleName(), "Internal", "Http"],
            fullyQualified: true
        });
    }

    public getEnvironmentsClassReference(): ruby.ClassReference {
        return ruby.classReference({
            name: "Environment",
            modules: [this.getRootModuleName()]
        });
    }

    public getDefaultEnvironmentClassReference() {
        const defaultEnvironmentId = this.ir.environments?.defaultEnvironment;
        if (defaultEnvironmentId == null) {
            return undefined;
        }

        // Lookup the default environment by id
        const defaultEnvironment = this.ir.environments?.environments.environments.find(
            (env) => env.id === defaultEnvironmentId
        );
        if (defaultEnvironment == null) {
            this.logger.warn(`Default environment ${defaultEnvironmentId} not found`);
            return undefined;
        }

        // Return the class reference, performing the same casing as the SingleUrlEnvironmentGenerator
        return ruby.classReference({
            name: defaultEnvironment.name.screamingSnakeCase.safeName,
            modules: [this.getRootModuleName(), "Environment"]
        });
    }

    public getRootClientClassName(): string {
        return this.customConfig.clientModuleName ?? "Client";
    }

    public getReferenceToInternalJSONRequest(): ruby.ClassReference {
        return ruby.classReference({
            name: "Request",
            modules: [this.getRootModuleName(), "Internal", "JSON"]
        });
    }

    public getReferenceToInternalMultipartRequest(): ruby.ClassReference {
        return ruby.classReference({
            name: "Request",
            modules: [this.getRootModuleName(), "Internal", "Multipart"]
        });
    }

    public getReferenceToTypeId(typeId: FernIr.TypeId): ruby.ClassReference {
        const typeDeclaration = this.getTypeDeclarationOrThrow(typeId);
        return ruby.classReference({
            name: typeDeclaration.name.name.pascalCase.safeName,
            modules: [
                this.getRootModuleName(),
                ...typeDeclaration.name.fernFilepath.allParts.map((path) => path.pascalCase.safeName),
                "Types"
            ]
        });
    }

    public getModuleNamesForServiceId(serviceId: FernIr.ServiceId): string[] {
        return [
            this.getRootModuleName(),
            ...this.getSubpackageForServiceId(serviceId).fernFilepath.allParts.map((part) => part.pascalCase.safeName),
            this.getTypesModule().name
        ];
    }

    public getModulesForServiceId(serviceId: FernIr.ServiceId): ruby.Module_[] {
        return this.getModuleNamesForServiceId(serviceId).map((part) => ruby.module({ name: part }));
    }

    public getRequestWrapperReference(serviceId: FernIr.ServiceId, requestName: FernIr.Name): ruby.ClassReference {
        return ruby.classReference({
            name: requestName.pascalCase.safeName,
            modules: this.getModuleNamesForServiceId(serviceId)
        });
    }

    public getCoreAsIsFiles(): string[] {
        const files = [
            // Public errors
            AsIsFiles.ApiError,
            AsIsFiles.ClientError,
            AsIsFiles.RedirectError,
            AsIsFiles.ResponseError,
            AsIsFiles.ServerError,
            AsIsFiles.TimeoutError,

            // Internal errors
            AsIsFiles.ErrorsConstraint,
            AsIsFiles.ErrorsType,

            // Iterators
            AsIsFiles.ItemIterator,
            AsIsFiles.CursorItemIterator,
            AsIsFiles.CursorPageIterator,
            AsIsFiles.OffsetItemIterator,
            AsIsFiles.OffsetPageIterator,
            ...(this.customConfig.customPagerName != null ? [AsIsFiles.CustomPager] : []),

            // HTTP
            AsIsFiles.HttpBaseRequest,
            AsIsFiles.HttpRawClient,

            // JSON
            AsIsFiles.JsonRequest,
            AsIsFiles.JsonSerializable,

            // Multipart
            AsIsFiles.MultipartEncoder,
            AsIsFiles.MultipartFormDataPart,
            AsIsFiles.MultipartFormData,
            AsIsFiles.MultipartRequest,

            // Types
            AsIsFiles.TypesModelField,
            AsIsFiles.TypesArray,
            AsIsFiles.TypesBoolean,
            AsIsFiles.TypesEnum,
            AsIsFiles.TypesHash,
            AsIsFiles.TypesModel,
            AsIsFiles.TypesType,
            AsIsFiles.TypesUnion,
            AsIsFiles.TypesUnknown,
            AsIsFiles.TypesUtils,

            // Tests
            AsIsFiles.TestHelper,
            AsIsFiles.TestCursorItemIterator,
            AsIsFiles.TestOffsetItemIterator,
            AsIsFiles.TestArrayType,
            AsIsFiles.TestBooleanType,
            AsIsFiles.TestEnumType,
            AsIsFiles.TestHashType,
            AsIsFiles.TestModelType,
            AsIsFiles.TestUnionType,
            AsIsFiles.TestTypeUtils
        ];

        return files;
    }

    public getInferredAuth(): FernIr.InferredAuthScheme | undefined {
        for (const scheme of this.ir.auth.schemes) {
            if (scheme.type === "inferred") {
                return scheme;
            }
        }
        return undefined;
    }

    public get selfHosted(): boolean {
        return this.ir.selfHosted ?? false;
    }

    public isMultipleBaseUrlsEnvironment(): boolean {
        return this.ir.environments?.environments.type === "multipleBaseUrls";
    }

    public getMultipleBaseUrlsEnvironments(): FernIr.MultipleBaseUrlsEnvironments | undefined {
        if (this.ir.environments?.environments.type === "multipleBaseUrls") {
            return this.ir.environments.environments;
        }
        return undefined;
    }

    public getDefaultBaseUrlId(): string | undefined {
        const multiUrlEnvs = this.getMultipleBaseUrlsEnvironments();
        if (multiUrlEnvs != null && multiUrlEnvs.baseUrls.length > 0) {
            return multiUrlEnvs.baseUrls[0]?.id;
        }
        return undefined;
    }

    public getBaseUrlName(baseUrlId: string): string | undefined {
        const multiUrlEnvs = this.getMultipleBaseUrlsEnvironments();
        if (multiUrlEnvs != null) {
            const baseUrl = multiUrlEnvs.baseUrls.find((b) => b.id === baseUrlId);
            return baseUrl?.name.snakeCase.safeName;
        }
        return undefined;
    }
}
