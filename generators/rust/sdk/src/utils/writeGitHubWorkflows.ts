import { RelativeFilePath } from "@fern-api/fs-utils";
import { RustFile } from "@fern-api/rust-base";
import { FernGeneratorExec } from "@fern-fern/generator-exec-sdk";
import { PublishingConfig } from "@fern-fern/ir-sdk/api";

export interface WriteGitHubWorkflowsArgs {
    githubOutputMode: FernGeneratorExec.GithubOutputMode;
    publishConfig: PublishingConfig | undefined;
    crateName: string;
}

export function generateGitHubWorkflowFiles(args: WriteGitHubWorkflowsArgs): RustFile[] {
    const files: RustFile[] = [];

    const workflowContent = constructWorkflowYaml(args);
    files.push(
        new RustFile({
            filename: "ci.yml",
            directory: RelativeFilePath.of(".github/workflows"),
            fileContents: workflowContent
        })
    );

    files.push(
        new RustFile({
            filename: ".gitignore",
            directory: RelativeFilePath.of("."),
            fileContents: constructGitignore()
        })
    );

    return files;
}

function constructWorkflowYaml({ githubOutputMode, crateName }: WriteGitHubWorkflowsArgs): string {
    let workflowYaml = `name: ci

on: [push]

jobs:
  compile:
    runs-on: ubuntu-latest
    steps:
      - name: Checkout repo
        uses: actions/checkout@v4

      - name: Install Rust toolchain
        uses: dtolnay/rust-toolchain@stable

      - name: Cache cargo registry
        uses: actions/cache@v4
        with:
          path: |
            ~/.cargo/registry
            ~/.cargo/git
            target
          key: \${{ runner.os }}-cargo-\${{ hashFiles('**/Cargo.lock') }}

      - name: Check formatting
        run: cargo fmt --all -- --check

      - name: Run clippy
        run: cargo clippy --all-targets --all-features -- -D warnings

      - name: Build
        run: cargo build --all-features

  test:
    runs-on: ubuntu-latest
    steps:
      - name: Checkout repo
        uses: actions/checkout@v4

      - name: Install Rust toolchain
        uses: dtolnay/rust-toolchain@stable

      - name: Cache cargo registry
        uses: actions/cache@v4
        with:
          path: |
            ~/.cargo/registry
            ~/.cargo/git
            target
          key: \${{ runner.os }}-cargo-\${{ hashFiles('**/Cargo.lock') }}

      - name: Run tests
        run: cargo test --all-features
`;

    const publishInfo = githubOutputMode.publishInfo;
    if (publishInfo != null && publishInfo.type === "crates") {
        const shouldGeneratePublishWorkflow =
            publishInfo.shouldGeneratePublishWorkflow == null || publishInfo.shouldGeneratePublishWorkflow === true;

        if (shouldGeneratePublishWorkflow) {
            const tokenEnvVar = publishInfo.tokenEnvironmentVariable ?? "CARGO_REGISTRY_TOKEN";

            workflowYaml += `
  publish:
    needs: [compile, test]
    if: github.event_name == 'push' && contains(github.ref, 'refs/tags/')
    runs-on: ubuntu-latest
    steps:
      - name: Checkout repo
        uses: actions/checkout@v4

      - name: Install Rust toolchain
        uses: dtolnay/rust-toolchain@stable

      - name: Cache cargo registry
        uses: actions/cache@v4
        with:
          path: |
            ~/.cargo/registry
            ~/.cargo/git
            target
          key: \${{ runner.os }}-cargo-\${{ hashFiles('**/Cargo.lock') }}

      - name: Publish to crates.io
        run: cargo publish
        env:
          CARGO_REGISTRY_TOKEN: \${{ secrets.${tokenEnvVar} }}
`;
        }
    }

    return workflowYaml;
}

function constructGitignore(): string {
    return `# Generated by Cargo
/target/
Cargo.lock

# IDE
/.idea/
/.vscode/
*.swp
*.swo

# OS
.DS_Store
Thumbs.db

# Logs
*.log

# Debug
debug/
`;
}
