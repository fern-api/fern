// Extracts CLI surface metadata from each embedded OpenAPI spec and writes
// Rust constant files consumed by the per-CLI smoke tests.
//
// For Fern-generated CLIs this step is skipped: the generator emits the
// equivalent constants directly at code-gen time, since it has already
// parsed the spec. The resulting smoke test structure is identical either way.

use std::collections::BTreeSet;
use std::path::Path;

fn main() {
    emit_cli_constants("box", "src/bin/box/openapi.yaml", "box_expected.rs");
}

/// Mirrors `parser.rs::camel_to_kebab` so the generated smoke-test constants
/// match what the parser produces at runtime. Any non-ASCII-alphanumeric run
/// collapses to a single dash (handles spaces, underscores, hyphens, dots,
/// etc.); each uppercase letter inserts a dash boundary; trailing dashes are
/// trimmed.
///
/// Kept as a copy rather than importing from `src/openapi/parser.rs` because
/// build scripts can't depend on the crate they're building. The two
/// implementations are byte-for-byte equivalent — see the round-trip test
/// `test_box_to_kebab_matches_parser_camel_to_kebab` in `parser.rs`.
fn to_kebab(s: &str) -> String {
    let mut result = String::with_capacity(s.len() + 4);
    for ch in s.chars() {
        if !ch.is_ascii_alphanumeric() {
            if !result.is_empty() && !result.ends_with('-') {
                result.push('-');
            }
        } else if ch.is_uppercase() {
            if !result.is_empty() && !result.ends_with('-') {
                result.push('-');
            }
            result.push(ch.to_lowercase().next().unwrap());
        } else {
            result.push(ch);
        }
    }
    while result.ends_with('-') {
        result.pop();
    }
    result
}

fn emit_cli_constants(cli_name: &str, spec_path: &str, out_file: &str) {
    println!("cargo:rerun-if-changed={spec_path}");

    let content = std::fs::read_to_string(spec_path)
        .unwrap_or_else(|e| panic!("failed to read {spec_path}: {e}"));
    let spec: serde_yaml::Value = serde_yaml::from_str(&content)
        .unwrap_or_else(|e| panic!("failed to parse {spec_path}: {e}"));

    let mut groups: BTreeSet<String> = BTreeSet::new();
    let mut op_count: usize = 0;

    let http_methods = ["get", "post", "put", "patch", "delete"];

    if let Some(paths) = spec.get("paths").and_then(|v| v.as_mapping()) {
        for (path_key, path_item) in paths {
            if let Some(item) = path_item.as_mapping() {
                for (method, operation) in item {
                    let is_http = method.as_str().is_some_and(|m| http_methods.contains(&m));
                    if !is_http {
                        continue;
                    }
                    op_count += 1;
                    // Mirror parser.rs's group resolution: x-fern-sdk-group-name → first tag
                    // → first path segment. Stays in sync so build.rs constants match the
                    // CLI surface even for operations that lack Fern extensions.
                    let top_group = operation
                        .get("x-fern-sdk-group-name")
                        .and_then(|v| v.as_sequence())
                        .and_then(|seq| seq.first())
                        .and_then(|v| v.as_str())
                        .map(str::to_string)
                        .or_else(|| {
                            operation
                                .get("tags")
                                .and_then(|v| v.as_sequence())
                                .and_then(|seq| seq.first())
                                .and_then(|v| v.as_str())
                                .map(str::to_string)
                        })
                        .or_else(|| {
                            path_key
                                .as_str()
                                .and_then(|p| p.trim_start_matches('/').split('/').next())
                                .map(str::to_string)
                        });
                    if let Some(g) = top_group {
                        groups.insert(to_kebab(&g));
                    }
                }
            }
        }
    }

    let prefix = cli_name.to_uppercase().replace('-', "_");
    let groups_literal = groups
        .iter()
        .map(|g| format!("    \"{g}\""))
        .collect::<Vec<_>>()
        .join(",\n");

    let generated = format!(
        "// Generated by build.rs from {spec_path} — do not edit\n\
         pub const {prefix}_EXPECTED_GROUPS: &[&str] = &[\n{groups_literal},\n];\n\
         pub const {prefix}_OPERATION_COUNT: usize = {op_count};\n"
    );

    let out_dir = std::env::var("OUT_DIR").unwrap();
    let out_path = Path::new(&out_dir).join(out_file);
    std::fs::write(&out_path, &generated)
        .unwrap_or_else(|e| panic!("failed to write {}: {e}", out_path.display()));
}
