import { CSharpFile } from "@fern-api/csharp-base";
import { FernIr } from "@fern-fern/ir-sdk";

import { SdkGeneratorContext } from "./SdkGeneratorContext.js";
import { BaseMockServerTestGenerator } from "./test-generation/mock-server/BaseMockServerTestGenerator.js";
import { GrpcMockServerTestGenerator } from "./test-generation/mock-server/GrpcMockServerTestGenerator.js";
import { GrpcStubGenerator } from "./test-generation/mock-server/GrpcStubGenerator.js";
import { MockServerTestGenerator } from "./test-generation/mock-server/MockServerTestGenerator.js";

export function generateSdkTests({ context }: { context: SdkGeneratorContext }): CSharpFile[] {
    const files: CSharpFile[] = [];
    files.push(...generateMockServerTests({ context }));
    files.push(...generateGrpcMockServerTests({ context }));
    return files;
}

function generateMockServerTests({ context }: { context: SdkGeneratorContext }): CSharpFile[] {
    if (!context.settings.shouldGenerateMockServerTests) {
        return [];
    }
    const files: CSharpFile[] = [];
    for (const [serviceId, service] of Object.entries(context.ir.services)) {
        if (shouldSkipMockServerTestsForService({ context, serviceId })) {
            continue;
        }
        for (const endpoint of service.endpoints) {
            if (shouldSkipMockServerTestForEndpoint({ endpoint })) {
                continue;
            }
            const allExamples = [...endpoint.autogeneratedExamples, ...endpoint.userSpecifiedExamples].map(
                (example) => example.example
            );
            // TODO: support other response body types
            const useableExamples = allExamples.filter((example): example is FernIr.ExampleEndpointCall => {
                const response = example?.response;
                return response?.type === "ok" && response.value.type === "body";
            });
            if (useableExamples.length === 0) {
                continue;
            }
            const testGenerator = new MockServerTestGenerator(context, useableExamples, endpoint, serviceId);
            if (!testGenerator.shouldGenerate()) {
                continue;
            }
            files.push(testGenerator.generate());
        }
    }
    if (files.length > 0) {
        // We should only generate the base if at least one mock test was generated.
        files.push(new BaseMockServerTestGenerator(context).generate());
    }
    return files;
}

function generateGrpcMockServerTests({ context }: { context: SdkGeneratorContext }): CSharpFile[] {
    if (!context.settings.shouldGenerateMockServerTests) {
        return [];
    }
    const files: CSharpFile[] = [];
    // Track generated stub classes per service to avoid duplicates
    const stubGeneratorsByService = new Map<string, GrpcStubGenerator>();

    // Skip if multi-URL environments without default (snippets don't support setting environment)
    if (
        context.ir.environments?.environments.type === "multipleBaseUrls" &&
        context.ir.environments.defaultEnvironment == null
    ) {
        return [];
    }

    // Skip if unions with base properties exist (snippets don't support them)
    if (
        Object.values(context.ir.types).find(
            (typeReference) => typeReference.shape.type === "union" && typeReference.shape.baseProperties.length > 0
        )
    ) {
        return [];
    }

    const servicesWithTests = new Set<string>();

    for (const [serviceId, service] of Object.entries(context.ir.services)) {
        const grpcClientInfo = context.getGrpcClientInfoForServiceId(serviceId);
        if (grpcClientInfo == null) {
            continue;
        }

        // Create a stub generator for this service if we haven't already
        let stubGenerator = stubGeneratorsByService.get(serviceId);
        if (stubGenerator == null) {
            stubGenerator = new GrpcStubGenerator(context, serviceId);
            stubGeneratorsByService.set(serviceId, stubGenerator);
        }

        for (const endpoint of service.endpoints) {
            // Skip HTTP-transport endpoints (they use WireMock tests)
            if (endpoint.transport?.type === "http") {
                continue;
            }
            if (shouldSkipMockServerTestForEndpoint({ endpoint })) {
                continue;
            }
            const allExamples = [...endpoint.autogeneratedExamples, ...endpoint.userSpecifiedExamples].map(
                (example) => example.example
            );
            const useableExamples = allExamples.filter((example): example is FernIr.ExampleEndpointCall => {
                const response = example?.response;
                return response?.type === "ok" && response.value.type === "body";
            });
            if (useableExamples.length === 0) {
                continue;
            }
            const testGenerator = new GrpcMockServerTestGenerator(
                context,
                useableExamples,
                endpoint,
                serviceId,
                grpcClientInfo,
                stubGenerator
            );
            files.push(testGenerator.generate());
            servicesWithTests.add(serviceId);
        }
    }

    // Generate stub files only for services that had at least one test
    for (const [serviceId, stubGenerator] of stubGeneratorsByService.entries()) {
        if (servicesWithTests.has(serviceId)) {
            files.push(stubGenerator.generate());
        }
    }

    return files;
}

function shouldSkipMockServerTestForEndpoint({ endpoint }: { endpoint: FernIr.HttpEndpoint }): boolean {
    const responseBodyType = endpoint.response?.body?.type;
    if (responseBodyType === "fileDownload" || responseBodyType === "streamParameter") {
        return true;
    }
    if (endpoint.response?.body?.type === "json" && endpoint.response.body.value.type === "nestedPropertyAsResponse") {
        return true;
    }
    if (endpoint.requestBody?.type === "bytes" || endpoint.requestBody?.type === "fileUpload") {
        return true;
    }
    return false;
}

function shouldSkipMockServerTestsForService({
    serviceId,
    context
}: {
    serviceId: string;
    context: SdkGeneratorContext;
}): boolean {
    // skip grpc clients from HTTP mock server tests (they have their own generator)
    if (context.getGrpcClientInfoForServiceId(serviceId) != null) {
        return true;
    }
    // don't support base properties on unions
    if (
        Object.values(context.ir.types).find(
            (typeReference) => typeReference.shape.type === "union" && typeReference.shape.baseProperties.length > 0
        )
    ) {
        return true;
    }
    // snippets don't support setting environment
    if (
        context.ir.environments?.environments.type === "multipleBaseUrls" &&
        context.ir.environments.defaultEnvironment == null
    ) {
        return true;
    }
    return false;
}
