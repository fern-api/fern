import { FERN_BOT_EMAIL, FERN_BOT_NAME, LockfileManager } from "@fern-api/replay";
import { execFileSync } from "child_process";
import { mkdirSync, writeFileSync } from "fs";
import { join } from "path";
import tmp from "tmp-promise";
import { replayForget } from "../replay/replay-forget";

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function gitExec(args: string[], cwd: string): string {
    return execFileSync("git", args, { cwd, encoding: "utf-8", stdio: "pipe" }).trim();
}

function initRepo(repoPath: string): void {
    gitExec(["init"], repoPath);
    gitExec(["config", "user.name", "Test User"], repoPath);
    gitExec(["config", "user.email", "test@example.com"], repoPath);
    gitExec(["config", "commit.gpgsign", "false"], repoPath);
}

function commitAsUser(repoPath: string, message: string): string {
    gitExec(["add", "."], repoPath);
    gitExec(["commit", "-m", message], repoPath);
    return gitExec(["rev-parse", "HEAD"], repoPath);
}

function commitAsFernBot(repoPath: string, message: string): string {
    gitExec(["add", "."], repoPath);
    gitExec(
        ["-c", `user.name=${FERN_BOT_NAME}`, "-c", `user.email=${FERN_BOT_EMAIL}`, "commit", "-m", message],
        repoPath
    );
    return gitExec(["rev-parse", "HEAD"], repoPath);
}

function createLockfile(repoPath: string, commitSha: string): void {
    const treeHash = gitExec(["rev-parse", `${commitSha}^{tree}`], repoPath);
    const lockManager = new LockfileManager(repoPath);
    lockManager.initialize({
        commit_sha: commitSha,
        tree_hash: treeHash,
        timestamp: new Date().toISOString(),
        cli_version: "0.1.0-test",
        generator_versions: { "test-generator": "1.0.0" }
    });
}

function addTestPatch(
    repoPath: string,
    genSha: string,
    patchId: string,
    commitSha: string,
    message: string,
    files: string[]
): void {
    const patchContent = gitExec(["format-patch", "-1", commitSha, "--stdout"], repoPath);
    const lockManager = new LockfileManager(repoPath);
    lockManager.read();
    lockManager.addPatch({
        id: patchId,
        original_commit: commitSha,
        original_author: "Test User <test@example.com>",
        original_message: message,
        base_generation: genSha,
        files,
        patch_content: patchContent,
        content_hash: `hash-${patchId}`
    });
    lockManager.save();
}

/**
 * Sets up a repo with a generation + two user patches:
 *   patch-aaa: modifies src/client.ts
 *   patch-bbb: adds src/helper.ts
 */
async function setupRepoWithPatches(): Promise<{ repoPath: string; genSha: string; cleanup: () => Promise<void> }> {
    const tmpDir = await tmp.dir({ unsafeCleanup: true });
    const repoPath = tmpDir.path;

    initRepo(repoPath);
    writeFileSync(join(repoPath, "README.md"), "# SDK\n");
    commitAsUser(repoPath, "initial commit");

    mkdirSync(join(repoPath, "src"), { recursive: true });
    writeFileSync(join(repoPath, "src/client.ts"), "export class Client {}\n");
    const genSha = commitAsFernBot(repoPath, "[fern-generated] Initial generation\n\nGenerated by Fern");

    createLockfile(repoPath, genSha);
    commitAsFernBot(repoPath, "[fern-replay] Applied customizations");

    // Patch 1: modify client.ts
    writeFileSync(join(repoPath, "src/client.ts"), "export class Client {\n  custom() {}\n}\n");
    const sha1 = commitAsUser(repoPath, "feat: add custom method");
    addTestPatch(repoPath, genSha, "patch-aaa11111", sha1, "feat: add custom method", ["src/client.ts"]);

    // Patch 2: add helper.ts
    writeFileSync(join(repoPath, "src/helper.ts"), "export function help() {}\n");
    const sha2 = commitAsUser(repoPath, "feat: add helper utility");
    addTestPatch(repoPath, genSha, "patch-bbb22222", sha2, "feat: add helper utility", ["src/helper.ts"]);

    return { repoPath, genSha, cleanup: tmpDir.cleanup };
}

// ---------------------------------------------------------------------------
// Tests
// ---------------------------------------------------------------------------

describe("replayForget", { tags: ["slow"] }, () => {
    describe("no lockfile", () => {
        let repoPath: string;
        let cleanup: () => Promise<void>;

        beforeAll(async () => {
            const tmpDir = await tmp.dir({ unsafeCleanup: true });
            repoPath = tmpDir.path;
            cleanup = tmpDir.cleanup;
            initRepo(repoPath);
            writeFileSync(join(repoPath, "README.md"), "# SDK\n");
            commitAsUser(repoPath, "initial commit");
        });

        afterAll(async () => {
            await cleanup?.();
        });

        it("returns initialized=false", () => {
            const result = replayForget({ outputDir: repoPath });
            expect(result.initialized).toBe(false);
        });
    });

    describe("--all mode", () => {
        let repoPath: string;
        let cleanup: () => Promise<void>;

        beforeAll(async () => {
            const setup = await setupRepoWithPatches();
            repoPath = setup.repoPath;
            cleanup = setup.cleanup;
        });

        afterAll(async () => {
            await cleanup?.();
        });

        it("removes all patches", () => {
            const result = replayForget({ outputDir: repoPath, options: { all: true } });
            expect(result.initialized).toBe(true);
            expect(result.removed).toHaveLength(2);
            expect(result.remaining).toBe(0);
        });
    });

    describe("--all --dry-run mode", () => {
        let repoPath: string;
        let cleanup: () => Promise<void>;

        beforeAll(async () => {
            const setup = await setupRepoWithPatches();
            repoPath = setup.repoPath;
            cleanup = setup.cleanup;
        });

        afterAll(async () => {
            await cleanup?.();
        });

        it("shows what would be removed without removing", () => {
            const result = replayForget({ outputDir: repoPath, options: { all: true, dryRun: true } });
            expect(result.removed).toHaveLength(2);

            // Verify patches still exist
            const lockManager = new LockfileManager(repoPath);
            const lock = lockManager.read();
            expect(lock.patches).toHaveLength(2);
        });
    });

    describe("patch ID mode", () => {
        let repoPath: string;
        let cleanup: () => Promise<void>;

        beforeAll(async () => {
            const setup = await setupRepoWithPatches();
            repoPath = setup.repoPath;
            cleanup = setup.cleanup;
        });

        afterAll(async () => {
            await cleanup?.();
        });

        it("removes specific patch by ID", () => {
            const result = replayForget({ outputDir: repoPath, options: { patchIds: ["patch-aaa11111"] } });
            expect(result.removed).toHaveLength(1);
            expect(result.removed[0]?.id).toBe("patch-aaa11111");
            expect(result.remaining).toBe(1);
        });

        it("reports already-forgotten IDs", () => {
            const result = replayForget({ outputDir: repoPath, options: { patchIds: ["patch-nonexistent"] } });
            expect(result.removed).toHaveLength(0);
            expect(result.alreadyForgotten).toContain("patch-nonexistent");
            expect(result.notFound).toBe(true);
        });
    });

    describe("pattern mode", () => {
        let repoPath: string;
        let cleanup: () => Promise<void>;

        beforeAll(async () => {
            const setup = await setupRepoWithPatches();
            repoPath = setup.repoPath;
            cleanup = setup.cleanup;
        });

        afterAll(async () => {
            await cleanup?.();
        });

        it("matches patches by file path", () => {
            const result = replayForget({ outputDir: repoPath, options: { pattern: "src/helper.ts" } });
            expect(result.matched).toHaveLength(1);
            expect(result.matched?.[0]?.id).toBe("patch-bbb22222");
        });

        it("matches patches by commit message substring", () => {
            const result = replayForget({ outputDir: repoPath, options: { pattern: "helper utility" } });
            expect(result.matched).toHaveLength(1);
            expect(result.matched?.[0]?.id).toBe("patch-bbb22222");
        });

        it("returns notFound when no match", () => {
            const result = replayForget({ outputDir: repoPath, options: { pattern: "nonexistent-file.ts" } });
            expect(result.notFound).toBe(true);
            expect(result.matched).toHaveLength(0);
        });
    });

    describe("no-args mode", () => {
        let repoPath: string;
        let cleanup: () => Promise<void>;

        beforeAll(async () => {
            const setup = await setupRepoWithPatches();
            repoPath = setup.repoPath;
            cleanup = setup.cleanup;
        });

        afterAll(async () => {
            await cleanup?.();
        });

        it("returns all patches as matched for selection", () => {
            const result = replayForget({ outputDir: repoPath });
            expect(result.matched).toHaveLength(2);
            expect(result.removed).toHaveLength(0);
        });
    });
});
