describe("first generation - no lockfile", { tags: ["slow", "flaky"] }, () => {
    let repoPath: string;
    let cleanup: () => Promise<void>;

    beforeAll(async () => {
        const tmpDir = await tmp.dir({ unsafeCleanup: true });
        repoPath = tmpDir.path;
        cleanup = tmpDir.cleanup;

        initRepo(repoPath);

        // Initial commit
        writeFileSync(join(repoPath, "README.md"), "# SDK\n");
        commitAsUser(repoPath, "initial commit");

        // Write generated files and commit as fern-bot — but NO lockfile
        mkdirSync(join(repoPath, "src"), { recursive: true });
        writeFileSync(
            join(repoPath, "src/client.ts"),
            'export class Client {\n  baseUrl = "https://api.example.com";\n}\n'
        );
        commitAsFernBot(repoPath, "[fern-generated] Initial SDK generation\n\nGenerated by Fern");
    });

    afterAll(async () => {
        await cleanup?.();
    });

    it("replayRun returns null report when no lockfile", async () => {
        const result = await replayRun({ outputDir: repoPath });

        expect(result.report).toBeNull();
        expect(result.fernignoreUpdated).toBe(false);
        expect(result.previousGenerationSha).toBeNull();
        expect(result.currentGenerationSha).toBeNull();
        expect(result.baseBranchHead).toBeNull();
    });
});

describe("first generation - with lockfile", { tags: ["slow", "flaky"] }, () => {
    let repoPath: string;
    let cleanup: () => Promise<void>;

    beforeAll(async () => {
        const setup = await setupRepoWithGeneration();
        repoPath = setup.repoPath;
        cleanup = setup.cleanup;

        // Write new (v2) generated files and stage them to simulate a new generation
        writeFileSync(
            join(repoPath, "src/client.ts"),
            'export class Client {\n  baseUrl = "https://api.example.com";\n  version = "2";\n}\n'
        );
        gitExec(["add", "."], repoPath);
    });

    afterAll(async () => {
        await cleanup?.();
    });

    it("ReplayService.runReplay handles first flow", async () => {
        const service = new ReplayService(repoPath, { enabled: true });
        const report = await service.runReplay({ cliVersion: "0.2.0", stageOnly: true });

        expect(report).not.toBeNull();
        expect(report.flow).toBeDefined();
    });
});

describe("normal regeneration with patches", { tags: ["slow", "flaky"] }, () => {
    let repoPath: string;
    let cleanup: () => Promise<void>;

    beforeAll(async () => {
        const setup = await setupRepoWithGeneration();
        repoPath = setup.repoPath;
        cleanup = setup.cleanup;

        // User customization: add a comment to client.ts as a regular user
        writeFileSync(
            join(repoPath, "src/client.ts"),
            'export class Client {\n  baseUrl = "https://api.example.com";\n  // user customization\n  customMethod() { return true; }\n}\n'
        );
        commitAsUser(repoPath, "feat: add custom method to Client");

        // New generation overwrites src/client.ts (different content)
        writeFileSync(
            join(repoPath, "src/client.ts"),
            'export class Client {\n  baseUrl = "https://api.example.com";\n  version = "2";\n}\n'
        );
        gitExec(["add", "."], repoPath);
    });

    afterAll(async () => {
        await cleanup?.();
    });

    it("detects and applies user patches", async () => {
        const service = new ReplayService(repoPath, { enabled: true });
        const report = await service.runReplay({ cliVersion: "0.2.0", stageOnly: true });

        expect(report).not.toBeNull();
        expect(report.patchesDetected).toBe(1);
    });
});

describe("conflict detection", { tags: ["slow", "flaky"] }, () => {
    let repoPath: string;
    let cleanup: () => Promise<void>;

    beforeAll(async () => {
        const setup = await setupRepoWithGeneration();
        repoPath = setup.repoPath;
        cleanup = setup.cleanup;

        // User edits the same line that the generator will change: baseUrl
        writeFileSync(
            join(repoPath, "src/client.ts"),
            'export class Client {\n  baseUrl = "https://custom.user.com";\n}\n'
        );
        commitAsUser(repoPath, "fix: use custom base URL");

        // New generation changes baseUrl to a different value on the same line
        writeFileSync(
            join(repoPath, "src/client.ts"),
            'export class Client {\n  baseUrl = "https://api-v2.example.com";\n}\n'
        );
        gitExec(["add", "."], repoPath);
    });

    afterAll(async () => {
        await cleanup?.();
    });

    it("reports conflicts on same-line edits", async () => {
        const service = new ReplayService(repoPath, { enabled: true });
        const report = await service.runReplay({ cliVersion: "0.2.0", stageOnly: true });

        expect(report).not.toBeNull();
        expect(report.patchesWithConflicts).toBeGreaterThanOrEqual(1);
    });

    it("provides conflict details", async () => {
        // Run a fresh service since the previous test already ran runReplay
        // We need a fresh repo state — use a separate tmp repo for this assertion
        const freshSetup = await setupRepoWithGeneration();
        const freshRepo = freshSetup.repoPath;

        try {
            // User edits baseUrl
            writeFileSync(
                join(freshRepo, "src/client.ts"),
                'export class Client {\n  baseUrl = "https://custom.user.com";\n}\n'
            );
            commitAsUser(freshRepo, "fix: use custom base URL");

            // Generator changes baseUrl to a different value
            writeFileSync(
                join(freshRepo, "src/client.ts"),
                'export class Client {\n  baseUrl = "https://api-v2.example.com";\n}\n'
            );
            gitExec(["add", "."], freshRepo);

            const service = new ReplayService(freshRepo, { enabled: true });
            const report = await service.runReplay({ cliVersion: "0.2.0", stageOnly: true });

            expect(report).not.toBeNull();
            expect(report.patchesWithConflicts).toBeGreaterThanOrEqual(1);

            const allConflictFiles = [
                ...(report.conflictDetails?.flatMap((d) => d.files.map((f) => f.file)) ?? []),
                ...report.conflicts.map((f) => f.file)
            ];
            expect(allConflictFiles.length).toBeGreaterThan(0);
            const hasClientTs = allConflictFiles.some((filePath) => filePath.includes("client.ts"));
            expect(hasClientTs).toBe(true);
        } finally {
            await freshSetup.cleanup();
        }
    });
});
