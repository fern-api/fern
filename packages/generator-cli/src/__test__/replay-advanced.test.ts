describe("error resilience", { tags: ["slow", "flaky"] }, () => {
    it("corrupted lockfile returns null report — does not throw", async () => {
        const tmpDir = await tmp.dir({ unsafeCleanup: true });
        const repoPath = tmpDir.path;
        try {
            gitExec(["init"], repoPath);
            gitExec(["config", "user.name", "Test User"], repoPath);
            gitExec(["config", "user.email", "test@example.com"], repoPath);

            writeFileSync(join(repoPath, "README.md"), "# SDK\n");
            gitExec(["add", "."], repoPath);
            gitExec(["commit", "-m", "initial commit"], repoPath);

            // Write a corrupt (invalid YAML/JSON) lockfile
            const fernDir = join(repoPath, ".fern");
            mkdirSync(fernDir, { recursive: true });
            writeFileSync(join(fernDir, "replay.lock"), "{ this is: [invalid yaml:\n  - broken\n");

            gitExec(["add", ".fern/replay.lock"], repoPath);
            gitExec(["commit", "-m", "add corrupted lockfile"], repoPath);

            // Should NOT throw
            const result = await replayRun({ outputDir: repoPath });

            // Corrupted lockfile should yield null report
            expect(result.report).toBeNull();
        } finally {
            await tmpDir.cleanup();
        }
    });

    it("logger receives warn or error on corrupted lockfile", async () => {
        const tmpDir = await tmp.dir({ unsafeCleanup: true });
        const repoPath = tmpDir.path;
        try {
            gitExec(["init"], repoPath);
            gitExec(["config", "user.name", "Test User"], repoPath);
            gitExec(["config", "user.email", "test@example.com"], repoPath);

            writeFileSync(join(repoPath, "README.md"), "# SDK\n");
            gitExec(["add", "."], repoPath);
            gitExec(["commit", "-m", "initial commit"], repoPath);

            // Write corrupt lockfile
            const fernDir = join(repoPath, ".fern");
            mkdirSync(fernDir, { recursive: true });
            writeFileSync(join(fernDir, "replay.lock"), "::: completely broken :::");

            gitExec(["add", ".fern/replay.lock"], repoPath);
            gitExec(["commit", "-m", "add corrupt lockfile"], repoPath);

            // Stage a file so replay actually has something to do
            mkdirSync(join(repoPath, "src"), { recursive: true });
            writeFileSync(join(repoPath, "src/api.ts"), "export const x = 1;\n");
            gitExec(["add", "."], repoPath);

            const mockLogger: PipelineLogger = {
                debug: vi.fn(),
                info: vi.fn(),
                warn: vi.fn(),
                error: vi.fn()
            };

            await replayRun({ outputDir: repoPath, logger: mockLogger });

            // Either warn or error must have been called — resilience path logs the failure
            const warnCalls = (mockLogger.warn as ReturnType<typeof vi.fn>).mock.calls.length;
            const errorCalls = (mockLogger.error as ReturnType<typeof vi.fn>).mock.calls.length;
            expect(warnCalls + errorCalls).toBeGreaterThan(0);
        } finally {
            await tmpDir.cleanup();
        }
    });
});

