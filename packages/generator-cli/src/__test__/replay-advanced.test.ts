describe("revert detection", { tags: ["slow", "flaky"] }, () => {
    let repoPath = "";
    let cleanup: () => Promise<void>;

    beforeAll(async () => {
        const setup = await setupRepoWithGeneration();
        repoPath = setup.repoPath;
        cleanup = setup.cleanup;

        // User makes a customization commit (NOT fern-bot)
        writeFileSync(
            join(repoPath, "src/client.ts"),
            'export class Client {\n  baseUrl = "https://custom.example.com";\n  timeout = 5000;\n}\n'
        );
        gitExec(["add", "src/client.ts"], repoPath);
        gitExecAs(["commit", "-m", "chore: add timeout to client"], repoPath, "Developer", "dev@example.com");

        const customCommitSha = gitExec(["rev-parse", "HEAD"], repoPath);

        // User reverts that commit
        gitExecAs(["revert", "--no-edit", customCommitSha], repoPath, "Developer", "dev@example.com");

        // Stage new generated files to simulate a new generation being ready
        writeFileSync(
            join(repoPath, "src/client.ts"),
            'export class Client {\n  baseUrl = "https://api.example.com";\n  version = "2.0.0";\n}\n'
        );
        gitExec(["add", "src/client.ts"], repoPath);
    });

    afterAll(async () => {
        await cleanup?.();
    });

    it("reverted patches cancel out — net patchesDetected is 0", async () => {
        const result = await replayRun({ outputDir: repoPath, stageOnly: true });
        // Lockfile exists so report must not be null
        expect(result.report).not.toBeNull();
        // When both the patch and its revert are present, they cancel out
        expect(result.report?.patchesDetected).toBe(0);
    });
});

