describe("fernignore idempotency", { tags: ["slow", "flaky"] }, () => {
    it(".fernignore created with replay entries after first call — returns true", async () => {
        const tmpDir = await tmp.dir({ unsafeCleanup: true });
        const outputDir = tmpDir.path;
        try {
            const updated = await ensureReplayFernignoreEntries(outputDir);
            expect(updated).toBe(true);

            const fernignorePath = join(outputDir, ".fernignore");
            expect(existsSync(fernignorePath)).toBe(true);

            const content = readFileSync(fernignorePath, "utf-8");
            for (const entry of REPLAY_FERNIGNORE_ENTRIES) {
                expect(content).toContain(entry);
            }
        } finally {
            await tmpDir.cleanup();
        }
    });

    it("second call does not duplicate entries — returns false", async () => {
        const tmpDir = await tmp.dir({ unsafeCleanup: true });
        const outputDir = tmpDir.path;
        try {
            // First call
            const firstResult = await ensureReplayFernignoreEntries(outputDir);
            expect(firstResult).toBe(true);

            // Second call on same dir
            const secondResult = await ensureReplayFernignoreEntries(outputDir);
            expect(secondResult).toBe(false);

            // No duplicate lines
            const content = readFileSync(join(outputDir, ".fernignore"), "utf-8");
            const lines = content.split("\n").filter((line) => line.trim().length > 0);
            for (const entry of REPLAY_FERNIGNORE_ENTRIES) {
                const occurrences = lines.filter((line) => line.trim() === entry).length;
                expect(occurrences).toBe(1);
            }
        } finally {
            await tmpDir.cleanup();
        }
    });
});
