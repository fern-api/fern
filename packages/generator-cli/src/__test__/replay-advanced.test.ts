function gitExec(args: string[], cwd: string): string {
    return execFileSync("git", args, { cwd, encoding: "utf-8", stdio: "pipe" }).trim();
}

function gitExecAs(args: string[], cwd: string, name: string, email: string): string {
    return execFileSync("git", ["-c", `user.name=${name}`, "-c", `user.email=${email}`, ...args], {
        cwd,
        encoding: "utf-8",
        stdio: "pipe"
    }).trim();
}

interface SetupResult {
    repoPath: string;
    cleanup: () => Promise<void>;
    generationSha: string;
    treeHash: string;
}

/**
 * Creates a git repo with an initial generation commit and a committed lockfile,
 * matching the recipe from the task description.
 */
async function setupRepoWithGeneration(): Promise<SetupResult> {
    const tmpDir = await tmp.dir({ unsafeCleanup: true });
    const repoPath = tmpDir.path;

    gitExec(["init"], repoPath);
    gitExec(["config", "user.name", "Test User"], repoPath);
    gitExec(["config", "user.email", "test@example.com"], repoPath);

    // Initial commit
    writeFileSync(join(repoPath, "README.md"), "# SDK\n");
    gitExec(["add", "."], repoPath);
    gitExec(["commit", "-m", "initial commit"], repoPath);

    // Generation commit (fern-bot)
    mkdirSync(join(repoPath, "src"), { recursive: true });
    writeFileSync(
        join(repoPath, "src/client.ts"),
        'export class Client {\n  baseUrl = "https://api.example.com";\n}\n'
    );
    gitExec(["add", "."], repoPath);
    gitExecAs(
        ["commit", "-m", "[fern-generated] Initial SDK generation\n\nGenerated by Fern"],
        repoPath,
        FERN_BOT_NAME,
        FERN_BOT_EMAIL
    );

    const generationSha = gitExec(["rev-parse", "HEAD"], repoPath);
    const treeHash = gitExec(["rev-parse", `${generationSha}^{tree}`], repoPath);

    // Write lockfile
    const lockManager = new LockfileManager(repoPath);
    lockManager.initialize({
        commit_sha: generationSha,
        tree_hash: treeHash,
        timestamp: new Date().toISOString(),
        cli_version: "0.1.0-test",
        generator_versions: { "test-generator": "1.0.0" }
    });

    // Commit the lockfile (as fern-bot)
    gitExec(["add", ".fern/replay.lock"], repoPath);
    gitExecAs(["commit", "-m", "[fern-replay] Applied customizations"], repoPath, FERN_BOT_NAME, FERN_BOT_EMAIL);

    return { repoPath, cleanup: tmpDir.cleanup, generationSha, treeHash };
}

const emptyContext: PipelineContext = {
    previousStepResults: {}
};

