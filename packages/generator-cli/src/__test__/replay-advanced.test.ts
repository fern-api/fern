describe("ReplayStep integration", { tags: ["slow", "flaky"] }, () => {
    let repoPath = "";
    let cleanup: () => Promise<void>;

    beforeAll(async () => {
        const setup = await setupRepoWithGeneration();
        repoPath = setup.repoPath;
        cleanup = setup.cleanup;
    });

    afterAll(async () => {
        await cleanup?.();
    });

    it("step name is 'replay'", () => {
        const mockLogger: PipelineLogger = {
            debug: vi.fn(),
            info: vi.fn(),
            warn: vi.fn(),
            error: vi.fn()
        };
        const config: ReplayStepConfig = { enabled: true, stageOnly: true };
        const step = new ReplayStep("/tmp/fake", mockLogger, config);
        expect(step.name).toBe("replay");
    });

    it("maps replayRun result to ReplayStepResult — has expected shape", async () => {
        const mockLogger: PipelineLogger = {
            debug: vi.fn(),
            info: vi.fn(),
            warn: vi.fn(),
            error: vi.fn()
        };
        const config: ReplayStepConfig = { enabled: true, stageOnly: true };
        const step = new ReplayStep(repoPath, mockLogger, config);

        const result = await step.execute(emptyContext);

        expect(result).toHaveProperty("executed", true);
        expect(result).toHaveProperty("success", true);
        expect(result).toHaveProperty("flow");
        expect(result.patchesDetected).toBe(0);
        expect(result.patchesApplied).toBe(0);
        expect(result.patchesWithConflicts).toBe(0);
    });

    it("first generation (no lockfile) returns flow 'first-generation'", async () => {
        const emptyTmpDir = await tmp.dir({ unsafeCleanup: true });
        const freshRepo = emptyTmpDir.path;
        try {
            gitExec(["init"], freshRepo);
            gitExec(["config", "user.name", "Test User"], freshRepo);
            gitExec(["config", "user.email", "test@example.com"], freshRepo);
            writeFileSync(join(freshRepo, "README.md"), "# Fresh\n");
            gitExec(["add", "."], freshRepo);
            gitExec(["commit", "-m", "initial commit"], freshRepo);

            const mockLogger: PipelineLogger = {
                debug: vi.fn(),
                info: vi.fn(),
                warn: vi.fn(),
                error: vi.fn()
            };
            const config: ReplayStepConfig = { enabled: true, stageOnly: true };
            const step = new ReplayStep(freshRepo, mockLogger, config);

            const result = await step.execute(emptyContext);

            // No lockfile means null report → step maps it to first-generation
            expect(result.executed).toBe(true);
            expect(result.success).toBe(true);
            expect(result.flow).toBe("first-generation");
            expect(result.patchesDetected).toBe(0);
            expect(result.patchesApplied).toBe(0);
            expect(result.patchesWithConflicts).toBe(0);
        } finally {
            await emptyTmpDir.cleanup();
        }
    });
});

