import { FERN_BOT_EMAIL, FERN_BOT_NAME, LockfileManager } from "@fern-api/replay";
import { execFileSync } from "child_process";
import { mkdirSync, writeFileSync } from "fs";
import { join } from "path";
import tmp from "tmp-promise";
import { replayStatus } from "../replay/replay-status";

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function gitExec(args: string[], cwd: string): string {
    return execFileSync("git", args, { cwd, encoding: "utf-8", stdio: "pipe" }).trim();
}

function initRepo(repoPath: string): void {
    gitExec(["init"], repoPath);
    gitExec(["config", "user.name", "Test User"], repoPath);
    gitExec(["config", "user.email", "test@example.com"], repoPath);
    gitExec(["config", "commit.gpgsign", "false"], repoPath);
}

function commitAsUser(repoPath: string, message: string): string {
    gitExec(["add", "."], repoPath);
    gitExec(["commit", "-m", message], repoPath);
    return gitExec(["rev-parse", "HEAD"], repoPath);
}

function commitAsFernBot(repoPath: string, message: string): string {
    gitExec(["add", "."], repoPath);
    gitExec(
        ["-c", `user.name=${FERN_BOT_NAME}`, "-c", `user.email=${FERN_BOT_EMAIL}`, "commit", "-m", message],
        repoPath
    );
    return gitExec(["rev-parse", "HEAD"], repoPath);
}

function createLockfile(repoPath: string, commitSha: string): void {
    const treeHash = gitExec(["rev-parse", `${commitSha}^{tree}`], repoPath);
    const lockManager = new LockfileManager(repoPath);
    lockManager.initialize({
        commit_sha: commitSha,
        tree_hash: treeHash,
        timestamp: new Date().toISOString(),
        cli_version: "0.1.0-test",
        generator_versions: { "test-generator": "1.0.0" }
    });
}

// ---------------------------------------------------------------------------
// Tests
// ---------------------------------------------------------------------------

describe("replayStatus", { tags: ["slow"] }, () => {
    describe("no lockfile", () => {
        let repoPath: string;
        let cleanup: () => Promise<void>;

        beforeAll(async () => {
            const tmpDir = await tmp.dir({ unsafeCleanup: true });
            repoPath = tmpDir.path;
            cleanup = tmpDir.cleanup;
            initRepo(repoPath);
            writeFileSync(join(repoPath, "README.md"), "# SDK\n");
            commitAsUser(repoPath, "initial commit");
        });

        afterAll(async () => {
            await cleanup?.();
        });

        it("returns initialized=false when no lockfile exists", () => {
            const result = replayStatus({ outputDir: repoPath });
            expect(result.initialized).toBe(false);
            expect(result.patches).toEqual([]);
            expect(result.generationCount).toBe(0);
        });
    });

    describe("with lockfile and no patches", () => {
        let repoPath: string;
        let cleanup: () => Promise<void>;

        beforeAll(async () => {
            const tmpDir = await tmp.dir({ unsafeCleanup: true });
            repoPath = tmpDir.path;
            cleanup = tmpDir.cleanup;

            initRepo(repoPath);
            writeFileSync(join(repoPath, "README.md"), "# SDK\n");
            commitAsUser(repoPath, "initial commit");

            mkdirSync(join(repoPath, "src"), { recursive: true });
            writeFileSync(join(repoPath, "src/client.ts"), "export class Client {}\n");
            const genSha = commitAsFernBot(repoPath, "[fern-generated] Initial generation\n\nGenerated by Fern");

            createLockfile(repoPath, genSha);
            commitAsFernBot(repoPath, "[fern-replay] Applied customizations");
        });

        afterAll(async () => {
            await cleanup?.();
        });

        it("returns initialized=true with generation info", () => {
            const result = replayStatus({ outputDir: repoPath });
            expect(result.initialized).toBe(true);
            expect(result.generationCount).toBe(1);
            expect(result.lastGeneration).toBeDefined();
            expect(result.lastGeneration?.cliVersion).toBe("0.1.0-test");
            expect(result.patches).toEqual([]);
            expect(result.unresolvedCount).toBe(0);
        });
    });

    describe("with lockfile and patches", () => {
        let repoPath: string;
        let cleanup: () => Promise<void>;

        beforeAll(async () => {
            const tmpDir = await tmp.dir({ unsafeCleanup: true });
            repoPath = tmpDir.path;
            cleanup = tmpDir.cleanup;

            initRepo(repoPath);
            writeFileSync(join(repoPath, "README.md"), "# SDK\n");
            commitAsUser(repoPath, "initial commit");

            mkdirSync(join(repoPath, "src"), { recursive: true });
            writeFileSync(join(repoPath, "src/client.ts"), "export class Client {}\n");
            const genSha = commitAsFernBot(repoPath, "[fern-generated] Initial generation\n\nGenerated by Fern");

            createLockfile(repoPath, genSha);
            commitAsFernBot(repoPath, "[fern-replay] Applied customizations");

            // User adds a customization
            writeFileSync(join(repoPath, "src/client.ts"), "export class Client {\n  custom() { return true; }\n}\n");
            const userCommitSha = commitAsUser(repoPath, "feat: add custom method");

            // Manually add a patch to the lockfile to simulate detected patches
            const lockManager = new LockfileManager(repoPath);
            lockManager.read();
            const patchContent = gitExec(["format-patch", "-1", userCommitSha, "--stdout"], repoPath);
            lockManager.addPatch({
                id: "patch-test12345",
                original_commit: userCommitSha,
                original_author: "Test User <test@example.com>",
                original_message: "feat: add custom method",
                base_generation: genSha,
                files: ["src/client.ts"],
                patch_content: patchContent,
                content_hash: "abc123"
            });
            lockManager.save();
        });

        afterAll(async () => {
            await cleanup?.();
        });

        it("returns patches with correct metadata", () => {
            const result = replayStatus({ outputDir: repoPath });
            expect(result.initialized).toBe(true);
            expect(result.patches).toHaveLength(1);

            const patch = result.patches[0];
            expect(patch).toBeDefined();
            expect(patch.id).toBe("patch-test12345");
            expect(patch.message).toBe("feat: add custom method");
            expect(patch.author).toBe("Test User");
            expect(patch.files).toContain("src/client.ts");
            expect(patch.fileCount).toBe(1);
            expect(patch.type).toBe("modified");
        });
    });
});
