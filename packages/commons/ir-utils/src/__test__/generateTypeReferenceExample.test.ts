import { ContainerType, PrimitiveTypeV2, Type, TypeDeclaration, TypeId, TypeReference } from "@fern-api/ir-sdk";

import { generateTypeReferenceExample } from "../examples/v1/generateTypeReferenceExample.js";

const DUMMY_FILEPATH = { allParts: [], packagePath: [], file: undefined };

function makeName(original: string) {
    const su = { safeName: original, unsafeName: original };
    return {
        originalName: original,
        camelCase: su,
        pascalCase: su,
        snakeCase: su,
        screamingSnakeCase: su
    };
}

function makeObjectTypeDeclaration(
    typeId: string,
    propertyNames: string[],
    propertyTypeRefs: TypeReference[]
): TypeDeclaration {
    return {
        name: {
            typeId,
            fernFilepath: DUMMY_FILEPATH,
            name: makeName(typeId),
            displayName: undefined
        },
        shape: Type.object({
            extends: [],
            properties: propertyNames.map((name, i) => ({
                docs: undefined,
                availability: undefined,
                name: { wireValue: name, name: makeName(name) },
                valueType: propertyTypeRefs[i] ?? stringRef(),
                propertyAccess: undefined,
                v2Examples: undefined
            })),
            extendedProperties: undefined,
            extraProperties: false
        }),
        autogeneratedExamples: [],
        userProvidedExamples: [],
        v2Examples: undefined,
        referencedTypes: new Set(),
        encoding: undefined,
        source: undefined,
        inline: undefined,
        docs: undefined,
        availability: undefined
    };
}

function namedRef(typeId: string): TypeReference {
    return TypeReference.named({
        typeId,
        fernFilepath: DUMMY_FILEPATH,
        name: makeName(typeId),
        displayName: undefined,
        default: undefined,
        inline: undefined
    });
}

function optionalNamedRef(typeId: string): TypeReference {
    return TypeReference.container(ContainerType.optional(namedRef(typeId)));
}

function stringRef(): TypeReference {
    return TypeReference.primitive({
        v1: "STRING",
        v2: PrimitiveTypeV2.string({ default: undefined, validation: undefined })
    });
}

describe("v1 cycle detection in generateTypeReferenceExample", () => {
    it("should detect self-referencing type with optional fields and not hang (simulates JsonschemaSchema with 14 self-refs)", () => {
        const selfRefFields = Array.from({ length: 14 }, (_, i) => `field${i}`);
        const typeDeclarations: Record<TypeId, TypeDeclaration> = {
            SelfRef: makeObjectTypeDeclaration(
                "SelfRef",
                [...selfRefFields, "name"],
                [...selfRefFields.map(() => optionalNamedRef("SelfRef")), stringRef()]
            )
        };

        const start = Date.now();
        const result = generateTypeReferenceExample({
            fieldName: undefined,
            typeReference: namedRef("SelfRef"),
            typeDeclarations,
            maxDepth: 10,
            currentDepth: 0,
            skipOptionalProperties: false
        });
        const elapsed = Date.now() - start;

        expect(elapsed).toBeLessThan(1000);
        expect(result.type).toBe("success");
        if (result.type === "success") {
            const json = result.jsonExample as Record<string, unknown>;
            expect(json).toHaveProperty("name");
            for (const field of selfRefFields) {
                const nested = json[field] as Record<string, unknown>;
                expect(nested).toBeDefined();
                expect(nested.name).toBe("name");
            }
        }
    });

    it("should generate stub when all required properties are recursive", () => {
        const typeDeclarations: Record<TypeId, TypeDeclaration> = {
            AllRequired: makeObjectTypeDeclaration("AllRequired", ["self"], [namedRef("AllRequired")])
        };

        const result = generateTypeReferenceExample({
            fieldName: undefined,
            typeReference: namedRef("AllRequired"),
            typeDeclarations,
            maxDepth: 10,
            currentDepth: 0,
            skipOptionalProperties: false
        });

        // Now succeeds: first visit generates the object, inner "self" property
        // hits cycle limit and gets a stub empty object instead of failing.
        // The stub itself is an object with a "self" property that is also a stub.
        expect(result.type).toBe("success");
        if (result.type === "success") {
            const json = result.jsonExample as Record<string, unknown>;
            expect(json).toHaveProperty("self");
            const inner = json.self as Record<string, unknown>;
            // The inner stub also has "self" because it recursed one more level
            // before hitting the limit. At the deepest level, "self" is an empty stub.
            expect(inner).toHaveProperty("self");
            expect(inner.self).toEqual({});
        }
    });

    it("should detect triangle cycle (A -> B -> C -> A) with optional back-edge", () => {
        const typeDeclarations: Record<TypeId, TypeDeclaration> = {
            A: makeObjectTypeDeclaration("A", ["toB", "value"], [namedRef("B"), stringRef()]),
            B: makeObjectTypeDeclaration("B", ["toC", "value"], [namedRef("C"), stringRef()]),
            C: makeObjectTypeDeclaration("C", ["toA", "value"], [optionalNamedRef("A"), stringRef()])
        };

        const result = generateTypeReferenceExample({
            fieldName: undefined,
            typeReference: namedRef("A"),
            typeDeclarations,
            maxDepth: 10,
            currentDepth: 0,
            skipOptionalProperties: false
        });

        expect(result.type).toBe("success");
        if (result.type === "success") {
            const json = result.jsonExample as Record<string, unknown>;
            expect(json).toHaveProperty("value");
            expect(json).toHaveProperty("toB");
            const toB = json.toB as Record<string, unknown>;
            expect(toB).toHaveProperty("value");
            expect(toB).toHaveProperty("toC");
            const toC = toB.toC as Record<string, unknown>;
            expect(toC).toHaveProperty("value");
            expect(toC).toHaveProperty("toA");
            const toA = toC.toA as Record<string, unknown>;
            expect(toA).toHaveProperty("value");
            expect(toA).toHaveProperty("toB");
            const toB2 = toA.toB as Record<string, unknown>;
            expect(toB2).toHaveProperty("value");
            expect(toB2).toHaveProperty("toC");
            const toC2 = toB2.toC as Record<string, unknown>;
            expect(toC2).toHaveProperty("value");
            // toA gets a stub with leaf properties and non-leaf named properties filled in recursively.
            // The visited set prevents infinite recursion (A is visited, so toA inside C's minimal stub
            // won't recurse back into A again).
            expect(toC2.toA).toEqual({
                value: "value",
                toB: {
                    value: "value",
                    toC: {
                        value: "value"
                    }
                }
            });
        }
    });

    it("should allow same type in sibling branches (backtracking correctness)", () => {
        const typeDeclarations: Record<TypeId, TypeDeclaration> = {
            Parent: makeObjectTypeDeclaration("Parent", ["child1", "child2"], [namedRef("Shared"), namedRef("Shared")]),
            Shared: makeObjectTypeDeclaration("Shared", ["data"], [stringRef()])
        };

        const result = generateTypeReferenceExample({
            fieldName: undefined,
            typeReference: namedRef("Parent"),
            typeDeclarations,
            maxDepth: 10,
            currentDepth: 0,
            skipOptionalProperties: false
        });

        expect(result.type).toBe("success");
        if (result.type === "success") {
            const json = result.jsonExample as Record<string, unknown>;
            expect(json.child1).toEqual({ data: "data" });
            expect(json.child2).toEqual({ data: "data" });
        }
    });

    it("should generate stubs for 3-way cycle with required fields (BulkSchedule-like)", () => {
        // Simulates: BulkSchedule -> multi(optional) -> MultiConfig -> schedules(required list) -> ItemSchedule -> schedule(required) -> BulkSchedule
        const enumRef = (): TypeReference =>
            TypeReference.primitive({
                v1: "STRING",
                v2: PrimitiveTypeV2.string({ default: undefined, validation: undefined })
            });
        const listRef = (typeId: string): TypeReference =>
            TypeReference.container(ContainerType.list(namedRef(typeId)));

        const typeDeclarations: Record<TypeId, TypeDeclaration> = {
            BulkSchedule: makeObjectTypeDeclaration(
                "BulkSchedule",
                ["frequency", "multi"],
                [enumRef(), optionalNamedRef("MultiConfig")]
            ),
            MultiConfig: makeObjectTypeDeclaration("MultiConfig", ["schedules"], [listRef("ItemSchedule")]),
            ItemSchedule: makeObjectTypeDeclaration(
                "ItemSchedule",
                ["item", "schedule"],
                [enumRef(), namedRef("BulkSchedule")]
            )
        };

        const result = generateTypeReferenceExample({
            fieldName: undefined,
            typeReference: namedRef("BulkSchedule"),
            typeDeclarations,
            maxDepth: 10,
            currentDepth: 0,
            skipOptionalProperties: false
        });

        expect(result.type).toBe("success");
        if (result.type === "success") {
            const json = result.jsonExample as Record<string, unknown>;
            expect(json).toHaveProperty("frequency");
            expect(json).toHaveProperty("multi");
            const multi = json.multi as Record<string, unknown>;
            expect(multi).toHaveProperty("schedules");
            const schedules = multi.schedules as Array<Record<string, unknown>>;
            // The list should have items (not be empty) because ItemSchedule.schedule
            // now gets a stub with leaf properties instead of failing
            expect(schedules.length).toBeGreaterThan(0);
            expect(schedules[0]).toHaveProperty("item");
            expect(schedules[0]).toHaveProperty("schedule");
            // The stub for BulkSchedule at cycle limit includes its leaf property "frequency"
            const stubSchedule = schedules[0]?.schedule as Record<string, unknown>;
            expect(stubSchedule).toHaveProperty("frequency");
        }
    });
});
