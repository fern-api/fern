import { V2SchemaExample, V2SchemaExamples } from "@fern-api/ir-sdk";

export function getFirstExamples(examples: V2SchemaExamples | undefined): {
    userExample: unknown;
    autoExample: unknown;
} {
    const userExamples = examples?.userSpecifiedExamples ?? {};
    const autoExamples = examples?.autogeneratedExamples ?? {};
    const firstUserExample = Object.values(userExamples)[0];
    const firstAutoExample = Object.values(autoExamples)[0];

    return {
        userExample: firstUserExample,
        autoExample: firstAutoExample
    };
}

export function getV2Examples(examples: V2SchemaExamples | undefined): {
    userExamples: Record<string, V2SchemaExample>;
    autoExamples: Record<string, V2SchemaExample>;
} {
    const userExamples = examples?.userSpecifiedExamples ?? {};
    const autoExamples = examples?.autogeneratedExamples ?? {};

    return {
        userExamples,
        autoExamples
    };
}
