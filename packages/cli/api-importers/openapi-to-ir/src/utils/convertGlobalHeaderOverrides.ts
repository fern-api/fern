import { RawSchemas } from "@fern-api/fern-definition-schema";
import { FernIr } from "@fern-api/ir-sdk";
import { AbstractConverter, AbstractConverterContext } from "@fern-api/v3-importer-commons";

function convertDefaultToLiteral(defaultValue: unknown): FernIr.Literal | undefined {
    if (defaultValue == null) {
        return undefined;
    }
    if (typeof defaultValue === "string") {
        return FernIr.Literal.string(defaultValue);
    }
    if (typeof defaultValue === "boolean") {
        return FernIr.Literal.boolean(defaultValue);
    }
    return undefined;
}

/**
 * Converts global header overrides from generators.yml (WithHeadersSchema format)
 * into FernIr.HttpHeader[] for the IR.
 */
export function convertGlobalHeaderOverrides({
    globalHeaderOverrides,
    context
}: {
    globalHeaderOverrides: RawSchemas.WithHeadersSchema;
    context: AbstractConverterContext<object>;
}): FernIr.HttpHeader[] {
    const headers: FernIr.HttpHeader[] = [];

    for (const [headerKey, headerSchema] of Object.entries(globalHeaderOverrides.headers ?? {})) {
        if (typeof headerSchema === "string") {
            headers.push({
                name: context.casingsGenerator.generateNameAndWireValue({
                    name: headerKey,
                    wireValue: headerKey
                }),
                valueType: AbstractConverter.STRING,
                env: undefined,
                v2Examples: {
                    userSpecifiedExamples: {},
                    autogeneratedExamples: {}
                },
                availability: undefined,
                docs: undefined,
                clientDefault: undefined
            });
        } else {
            const name = headerSchema.name ?? headerKey;
            headers.push({
                name: context.casingsGenerator.generateNameAndWireValue({
                    name,
                    wireValue: headerKey
                }),
                valueType: AbstractConverter.STRING,
                env: headerSchema.env,
                v2Examples: {
                    userSpecifiedExamples: {},
                    autogeneratedExamples: {}
                },
                availability: undefined,
                docs: headerSchema.docs,
                clientDefault: convertDefaultToLiteral(headerSchema.default)
            });
        }
    }

    return headers;
}
