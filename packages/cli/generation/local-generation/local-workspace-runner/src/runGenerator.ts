function extractLicenseFilePath(
    generatorInvocation: generatorsYml.GeneratorInvocation,
    absolutePathToFernConfig?: AbsoluteFilePath
): AbsoluteFilePath | undefined {
    const licenseConfig = getLicensePathFromConfig(generatorInvocation);

    if (licenseConfig?.type === "custom") {
        const licensePath = licenseConfig.value;
        const baseDir = absolutePathToFernConfig ? path.dirname(absolutePathToFernConfig) : process.cwd();
        const absoluteLicensePath = path.isAbsolute(licensePath) ? licensePath : path.resolve(baseDir, licensePath);
        return AbsoluteFilePath.of(absoluteLicensePath);
    }

    return undefined;
}

export async function writeFilesToDiskAndRunGenerator({
    organization,
    absolutePathToFernConfig,
    workspace,
    generatorInvocation,
    absolutePathToLocalOutput,
    absolutePathToLocalSnippetJSON,
    absolutePathToLocalSnippetTemplateJSON,
    audiences,
    version,
    workspaceTempDir,
    keepDocker,
    context,
    irVersionOverride,
    outputVersionOverride,
    writeUnitTests,
    generateOauthClients,
    generatePaginatedClients,
    includeOptionalRequestPropertyExamples,
    inspect,
    executionEnvironment,
    runner,
    whiteLabel,
    publishToRegistry,
    ir,
    ai,
    autoVersioningCache,
    absolutePathToSpecRepo,
    skipFernignore,
    disableTelemetry,
    deferredExampleGeneration,
    onDeferredWorkComplete
}: {
    organization: string;
    absolutePathToFernConfig: AbsoluteFilePath | undefined;
    workspace: FernWorkspace;
    generatorInvocation: generatorsYml.GeneratorInvocation;
    version: string | undefined;
    absolutePathToLocalOutput: AbsoluteFilePath;
    absolutePathToLocalSnippetJSON: AbsoluteFilePath | undefined;
    absolutePathToLocalSnippetTemplateJSON: AbsoluteFilePath | undefined;
    audiences: Audiences;
    workspaceTempDir: tmp.DirectoryResult;
    keepDocker: boolean;
    context: TaskContext;
    irVersionOverride: string | undefined;
    outputVersionOverride: string | undefined;
    writeUnitTests: boolean;
    generateOauthClients: boolean;
    generatePaginatedClients: boolean;
    includeOptionalRequestPropertyExamples: boolean;
    inspect: boolean;
    executionEnvironment?: ExecutionEnvironment;
    runner: ContainerRunner | undefined;
    whiteLabel?: boolean;
    publishToRegistry?: boolean;
    ir: IntermediateRepresentation;
    ai: generatorsYml.AiServicesSchema | undefined;
    autoVersioningCache?: AutoVersioningCache;
    absolutePathToSpecRepo: AbsoluteFilePath | undefined;
    skipFernignore?: boolean;
    disableTelemetry?: boolean;
    /** When set, example generation is deferred to overlap with container execution. */
    deferredExampleGeneration?: {
        exampleGeneration: ExampleGenerationArgs;
        generationLanguage: generatorsYml.GenerationLanguage | undefined;
        smartCasing: boolean;
    };
    /**
     * EXP-042: Callback invoked after deferred IR work completes, runs concurrently with Docker.
     * Returns file data to be written to disk after copyGeneratedFiles (avoids rm -rf conflict).
     */
    onDeferredWorkComplete?: (
        ir: IntermediateRepresentation,
        config: FernGeneratorExec.GeneratorConfig
    ) => Promise<Array<{ absolutePath: string; content: string }>>;
}): Promise<{
    ir: IntermediateRepresentation;
    generatorConfig: FernGeneratorExec.GeneratorConfig;
    preGeneratedFiles?: Array<{ absolutePath: string; content: string }>;
    shouldCommit: boolean;
    autoVersioningCommitMessage?: string;
    autoVersioningChangelogEntry?: string;
    autoVersioningPrDescription?: string;
    autoVersioningVersionBumpReason?: string;
    autoVersioningVersionBump?: string;
    autoVersioningNewVersion?: string;
    autoVersioningPreviousVersion?: string;
}> {
    const t0 = Date.now();

    // For Java generators with deferred example generation: strip examples and dynamic
    // from the v1 IR to reduce migration/serialization/deserialization cost.
    // - Examples are only used by v2 (via FULL_IR_PATH)
    // - Dynamic (4.78MB for Square API) is only used by v2 (via FULL_IR_PATH)
    // - v1 reads only from the regular IR path and doesn't use either field
    // Non-Java generators get the unmodified IR.
    const migrationIr =
        deferredExampleGeneration != null
            ? (stripExamplesAndDynamicFromIr(ir) as IntermediateRepresentation)
            : ir;
    context.logger.info(`[perf] prepareIR: ${Date.now() - t0}ms`);

    const t1 = Date.now();
    const { migrated } = await getIntermediateRepresentation({
        workspace,
        audiences,
        generatorInvocation,
        context,
        irVersionOverride,
        packageName: generatorsYml.getPackageName({ generatorInvocation }),
        version: version ?? outputVersionOverride,
        sourceConfig: getSourceConfig(workspace, executionEnvironment?.usesContainerPaths ?? true),
        includeOptionalRequestPropertyExamples,
        ir: migrationIr
    });
    context.logger.info(`[perf] getIntermediateRepresentation: ${Date.now() - t1}ms`);

    const t2 = Date.now();
    const absolutePathToIr = await writeIrToFile({
        workspaceTempDir,
        filename: IR_FILENAME,
        context,
        ir: migrated
    });
    context.logger.info(`[perf] writeV1IR: ${Date.now() - t2}ms`);

    // Reserve path for full IR
    const fullIrFilename = "ir-full.json";
    const absolutePathToFullIr = AbsoluteFilePath.of(join(workspaceTempDir.path, fullIrFilename));

    const configJsonFile = join(workspaceTempDir.path, GENERATOR_CONFIG_FILENAME);
    const absolutePathToWriteConfigJson = AbsoluteFilePath.of(configJsonFile);
    await writeFile(configJsonFile, "");

    const tmpOutputDirectory = join(workspaceTempDir.path, CODEGEN_OUTPUT_DIRECTORY_NAME);
    const absolutePathToTmpOutputDirectory = AbsoluteFilePath.of(tmpOutputDirectory);
    await mkdir(tmpOutputDirectory, { recursive: true });

    let absolutePathToTmpSnippetJSON: AbsoluteFilePath | undefined;
    if (absolutePathToLocalSnippetJSON != null) {
        const snippetJsonFile = join(workspaceTempDir.path, "snippet.json");
        absolutePathToTmpSnippetJSON = AbsoluteFilePath.of(snippetJsonFile);
        await writeFile(snippetJsonFile, "");
    }

    let absolutePathToTmpSnippetTemplatesJSON: AbsoluteFilePath | undefined;
    if (absolutePathToLocalSnippetTemplateJSON != null) {
        const snippetTemplatesJsonFile = join(workspaceTempDir.path, SNIPPET_TEMPLATES_JSON_FILENAME);
        absolutePathToTmpSnippetTemplatesJSON = AbsoluteFilePath.of(snippetTemplatesJsonFile);
        await writeFile(snippetTemplatesJsonFile, "");
    }

    const environment =
        executionEnvironment ??
        new ContainerExecutionEnvironment({
            containerImage: generatorInvocation.containerImage
                ? `${generatorInvocation.containerImage}:${generatorInvocation.version}`
                : `${generatorInvocation.name}:${generatorInvocation.version}`,
            keepContainer: keepDocker,
            disableTelemetry
        });

    const paths = environment.usesContainerPaths
        ? ({
              outputDirectory: AbsoluteFilePath.of(CONTAINER_CODEGEN_OUTPUT_DIRECTORY),
              irPath: AbsoluteFilePath.of(CONTAINER_PATH_TO_IR),
              configPath: AbsoluteFilePath.of(CONTAINER_GENERATOR_CONFIG_PATH),
              snippetPath: AbsoluteFilePath.of(CONTAINER_PATH_TO_SNIPPET),
              snippetTemplatePath: AbsoluteFilePath.of(CONTAINER_PATH_TO_SNIPPET_TEMPLATES)
          } as const)
        : ({
              outputDirectory: absolutePathToTmpOutputDirectory,
              configPath: absolutePathToWriteConfigJson,
              irPath: absolutePathToIr,
              snippetPath: absolutePathToTmpSnippetJSON,
              snippetTemplatePath: absolutePathToTmpSnippetTemplatesJSON
          } as const);

    const generatorLanguage =
        generatorInvocation.language ?? extractLanguageFromGeneratorName(generatorInvocation.name);
    const mappedVersion = version != null ? mapMagicVersionForLanguage(version, generatorLanguage) : version;
    const mappedOutputVersionOverride =
        outputVersionOverride != null
            ? mapMagicVersionForLanguage(outputVersionOverride, generatorLanguage)
            : outputVersionOverride;

    const config = getGeneratorConfig({
        generatorInvocation,
        customConfig: generatorInvocation.config,
        workspaceName: workspace.definition.rootApiFile.contents.name,
        outputVersion: mappedOutputVersionOverride,
        organization,
        absolutePathToSnippet: absolutePathToTmpSnippetJSON,
        absolutePathToSnippetTemplates: absolutePathToTmpSnippetTemplatesJSON,
        absolutePathToFernConfig,
        writeUnitTests,
        generateOauthClients,
        generatePaginatedClients,
        whiteLabel,
        publishToRegistry,
        paths
    });

    await writeFile(
        absolutePathToWriteConfigJson,
        JSON.stringify(await GeneratorConfig.jsonOrThrow(config), undefined, 4)
    );

    const absolutePathToLicenseFile = extractLicenseFilePath(generatorInvocation, absolutePathToFernConfig);
    const sourceMounts = workspace
        .getSources()
        .filter((source): source is IdentifiableSource & { type: "protobuf" } => source.type === "protobuf")
        .map((source) => ({
            hostPath: source.absoluteFilePath,
            containerPath: `${CONTAINER_SOURCES_DIRECTORY}/${source.id}`
        }));

    const t4 = Date.now();

    let preGeneratedFiles: Array<{ absolutePath: string; content: string }> | undefined;

    if (deferredExampleGeneration != null) {
        // EXP-022: Deferred example generation — overlap with container execution.
        // Write placeholder for FULL_IR_PATH (Docker bind mount requires the file to exist).
        // The real full IR is written by the deferred work before v2 reads it (~800ms margin).
        await writeFile(absolutePathToFullIr, "{}");

        const deferredWorkAndSnippets = async (): Promise<void> => {
            const dStart = Date.now();
            injectAutogeneratedExamples({
                ir,
                exampleGeneration: deferredExampleGeneration.exampleGeneration
            });
            const dynamic = convertIrToDynamicSnippetsIr({
                ir,
                generationLanguage: deferredExampleGeneration.generationLanguage,
                smartCasing: deferredExampleGeneration.smartCasing
            });
            ir.dynamic = dynamic;
            const fullIrForV2 = environment.usesContainerPaths
                ? JSON.stringify(stripExamplesFromIr({ ...ir, dynamic }))
                : JSON.stringify(ir);
            await writeFile(absolutePathToFullIr, fullIrForV2);
            context.logger.info(`[perf] deferredExampleWork: ${Date.now() - dStart}ms`);

            // EXP-042: Generate snippet tests in memory while Docker is still running.
            // The IR is now ready (examples injected, dynamic computed). CPU-bound snippet
            // generation overlaps with Docker container execution (~755ms savings).
            if (onDeferredWorkComplete != null) {
                const sStart = Date.now();
                preGeneratedFiles = await onDeferredWorkComplete(ir, config);
                context.logger.info(`[perf] preGenerateSnippets: ${Date.now() - sStart}ms`);
            }
        };

        await Promise.all([
            environment.execute({
                generatorName: generatorInvocation.name,
                irPath: absolutePathToIr,
                fullIrPath: absolutePathToFullIr,
                configPath: absolutePathToWriteConfigJson,
                outputPath: absolutePathToTmpOutputDirectory,
                snippetPath: absolutePathToTmpSnippetJSON,
                snippetTemplatePath: absolutePathToTmpSnippetTemplatesJSON,
                licenseFilePath: absolutePathToLicenseFile,
                sourceMounts,
                context,
                inspect,
                runner,
                workspaceTempDir: environment.usesContainerPaths ? workspaceTempDir.path : undefined
            }),
            deferredWorkAndSnippets()
        ]);
    } else {
        // Default path: write full IR (unmodified) synchronously, then execute container.
        // Non-Java generators need the complete IR with examples and dynamic.
        await writeFile(absolutePathToFullIr, JSON.stringify(ir));

        await environment.execute({
            generatorName: generatorInvocation.name,
            irPath: absolutePathToIr,
            fullIrPath: absolutePathToFullIr,
            configPath: absolutePathToWriteConfigJson,
            outputPath: absolutePathToTmpOutputDirectory,
            snippetPath: absolutePathToTmpSnippetJSON,
            snippetTemplatePath: absolutePathToTmpSnippetTemplatesJSON,
            licenseFilePath: absolutePathToLicenseFile,
            sourceMounts,
            context,
            inspect,
            runner,
            workspaceTempDir: environment.usesContainerPaths ? workspaceTempDir.path : undefined
        });
    }
    context.logger.info(`[perf] execute: ${Date.now() - t4}ms`);

    context.logger.info(`[perf] total orchestration: ${Date.now() - t0}ms`);

    const taskHandler = new LocalTaskHandler({
        context,
        absolutePathToLocalOutput,
        absolutePathToTmpOutputDirectory,
        absolutePathToLocalSnippetJSON,
        absolutePathToLocalSnippetTemplateJSON,
        absolutePathToTmpSnippetJSON,
        absolutePathToTmpSnippetTemplatesJSON,
        version: mappedVersion,
        ai,
        isWhitelabel: ir.readmeConfig?.whiteLabel ?? false,
        autoVersioningCache,
        generatorLanguage,
        absolutePathToSpecRepo,
        skipFernignore
    });
    const generatedFilesResult = await taskHandler.copyGeneratedFiles();

    // EXP-042: Write pre-generated snippet files after copyGeneratedFiles
    // (which may rm -rf the output directory).
    if (preGeneratedFiles != null && preGeneratedFiles.length > 0) {
        const wStart = Date.now();
        await Promise.all(
            preGeneratedFiles.map(async ({ absolutePath, content }) => {
                await mkdir(path.dirname(absolutePath), { recursive: true });
                await writeFile(absolutePath, content);
            })
        );
        context.logger.info(`[perf] writePreGeneratedSnippets: ${Date.now() - wStart}ms (${preGeneratedFiles.length} files)`);
    }

    return {
        ir,
        generatorConfig: config,
        preGeneratedFiles,
        ...generatedFilesResult
    };
}

async function writeIrToFile({
    ir,
    workspaceTempDir,
    filename,
    context
}: {
    ir: unknown;
    workspaceTempDir: DirectoryResult;
    filename?: string;
    context: TaskContext;
}): Promise<AbsoluteFilePath> {
    context.logger.debug("Migrated IR");
    let absolutePathToIr: AbsoluteFilePath;
    if (filename) {
        absolutePathToIr = AbsoluteFilePath.of(join(workspaceTempDir.path, filename));
    } else {
        const irFile = await tmp.file({
            tmpdir: workspaceTempDir.path
        });
        absolutePathToIr = AbsoluteFilePath.of(irFile.path);
    }
    await streamObjectToFile(absolutePathToIr, ir, { pretty: false });
    context.logger.debug(`Wrote IR to ${absolutePathToIr}`);
    return absolutePathToIr;
}

function getSourceConfig(workspace: FernWorkspace, usesContainerPaths: boolean): SourceConfig {
    return {
        sources: workspace.getSources().map((source) => {
            if (source.type === "protobuf") {
                const protoRootUrl = usesContainerPaths
                    ? `file:///${getDockerDestinationForSource(source)}`
                    : `file:///${source.absoluteFilePath}`;
                return ApiDefinitionSource.proto({
                    id: source.id,
                    protoRootUrl
                });
            }
            return ApiDefinitionSource.openapi();
        })
    };
}

function getDockerDestinationForSource(source: IdentifiableSource): string {
    return `${CONTAINER_SOURCES_DIRECTORY}/${source.id}`;
}

/**
 * Strips autogeneratedExamples and userSpecifiedExamples from all endpoints in the IR.
 * Examples typically account for ~97% of the IR size (e.g., 52MB of 57MB for Square API)
 * but are not used by the v1 Java generator. The v2 generator reads examples from a
 * separate full IR file via the FULL_IR_PATH environment variable.
 */
function stripExamplesFromIr(ir: unknown): unknown {
    if (ir == null || typeof ir !== "object") {
        return ir;
    }
    const irObj = ir as Record<string, unknown>;
    const services = irObj.services;
    if (services == null || typeof services !== "object") {
        return ir;
    }
    const strippedServices: Record<string, unknown> = {};
    for (const [key, service] of Object.entries(services as Record<string, unknown>)) {
        if (service == null || typeof service !== "object") {
            strippedServices[key] = service;
            continue;
        }
        const svcObj = service as Record<string, unknown>;
        const endpoints = svcObj.endpoints;
        if (!Array.isArray(endpoints)) {
            strippedServices[key] = service;
            continue;
        }
        strippedServices[key] = {
            ...svcObj,
            endpoints: endpoints.map((ep: unknown) => {
                if (ep == null || typeof ep !== "object") {
                    return ep;
                }
                const epObj = ep as Record<string, unknown>;
                return {
                    ...epObj,
                    autogeneratedExamples: [],
                    userSpecifiedExamples: []
                };
            })
        };
    }
    return { ...irObj, services: strippedServices };
}

/**
 * Strips examples (from endpoints) and the dynamic field from the IR.
 * Used for the v1 IR path when FULL_IR_PATH is available for v2:
 * - Examples: only used by v2 for snippet generation
 * - Dynamic (4.78MB for Square API): only used by v2 for dynamic snippets
 * Both are available to v2 via FULL_IR_PATH.
 */
function stripExamplesAndDynamicFromIr(ir: unknown): unknown {
    const stripped = stripExamplesFromIr(ir);
    if (stripped == null || typeof stripped !== "object") {
        return stripped;
    }
    const { dynamic: _, ...rest } = stripped as Record<string, unknown>;
    return rest;
}
