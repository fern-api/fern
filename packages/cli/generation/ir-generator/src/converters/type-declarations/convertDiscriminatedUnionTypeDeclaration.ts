import { isRawObjectDefinition, RawSchemas } from "@fern-api/fern-definition-schema";
import {
    SingleUnionType,
    SingleUnionTypeProperties,
    Type,
    TypeReference,
    UnionDiscriminatorContext
} from "@fern-api/ir-sdk";

import { FernFileContext } from "../../FernFileContext.js";
import { ResolvedType } from "../../resolvers/ResolvedType.js";
import { TypeResolver } from "../../resolvers/TypeResolver.js";
import { getAvailability } from "../../utils/getAvailability.js";
import { getDisplayName } from "../../utils/getDisplayName.js";
import { getDocs } from "../../utils/getDocs.js";
import { parseTypeName } from "../../utils/parseTypeName.js";
import { convertDeclaration } from "../convertDeclaration.js";
import { getExtensionsAsList, getPropertyAccess, getPropertyName } from "./convertObjectTypeDeclaration.js";

const DEFAULT_UNION_VALUE_PROPERTY_VALUE = "value";

export function convertDiscriminatedUnionTypeDeclaration({
    union,
    file,
    typeResolver
}: {
    union: RawSchemas.DiscriminatedUnionSchema;
    file: FernFileContext;
    typeResolver: TypeResolver;
}): Type {
    const discriminant = getUnionDiscriminant(union);
    const defaultVariantKey = union["default-variant"];
    const unionTypes = Object.entries(union.union).map(([unionKey, rawSingleUnionType]): SingleUnionType => {
        const rawType: string | undefined =
            typeof rawSingleUnionType === "string"
                ? rawSingleUnionType
                : typeof rawSingleUnionType.type === "string"
                  ? rawSingleUnionType.type
                  : undefined;

        const discriminantValue = file.casingsGenerator.generateNameAndWireValue({
            wireValue: unionKey,
            name: getSingleUnionTypeName({ unionKey, rawSingleUnionType }).name
        });

        const docs = getDocs(rawSingleUnionType);

        if (rawType == null) {
            return {
                discriminantValue,
                docs,
                shape: SingleUnionTypeProperties.noProperties(),
                displayName: undefined,
                availability: undefined
            };
        }

        const parsedValueType = file.parseTypeReference(rawType);

        return {
            discriminantValue,
            shape: getSingleUnionTypeProperties({
                rawSingleUnionType,
                rawValueType: rawType,
                parsedValueType,
                file,
                typeResolver
            }),
            docs: getDocs(rawSingleUnionType),
            displayName: getDisplayName(rawSingleUnionType),
            availability: getAvailability(rawSingleUnionType)
        };
    });
    const defaultUnionType =
        defaultVariantKey != null
            ? unionTypes.find((ut) => {
                  const wireValue =
                      typeof ut.discriminantValue === "string" ? ut.discriminantValue : ut.discriminantValue.wireValue;
                  return wireValue === defaultVariantKey;
              })
            : undefined;
    return Type.union({
        discriminant: file.casingsGenerator.generateNameAndWireValue({
            wireValue: discriminant,
            name: getUnionDiscriminantName(union).name
        }),
        extends: getExtensionsAsList(union.extends).map((extended) => parseTypeName({ typeName: extended, file })),
        baseProperties:
            union["base-properties"] != null
                ? Object.entries(union["base-properties"]).map(([propertyKey, propertyDefinition]) => ({
                      ...convertDeclaration(propertyDefinition),
                      name: file.casingsGenerator.generateNameAndWireValue({
                          wireValue: propertyKey,
                          name: getPropertyName({ propertyKey, property: propertyDefinition }).name
                      }),
                      valueType: file.parseTypeReference(propertyDefinition),
                      propertyAccess: getPropertyAccess({ property: propertyDefinition }),
                      v2Examples: {
                          userSpecifiedExamples: {},
                          autogeneratedExamples: {}
                      }
                  }))
                : [],
        types: unionTypes,
        default: defaultUnionType,
        discriminatorContext: getDiscriminatorContext({ union })
    });
}

export function getUnionDiscriminant(union: RawSchemas.DiscriminatedUnionSchema): string {
    if (union.discriminant == null) {
        return "type";
    }
    if (typeof union.discriminant === "string") {
        return union.discriminant;
    }
    return union.discriminant.value;
}

export function getUnionDiscriminantName(union: RawSchemas.DiscriminatedUnionSchema): {
    name: string;
    wasExplicitlySet: boolean;
} {
    if (union.discriminant != null && typeof union.discriminant !== "string" && union.discriminant.name != null) {
        return {
            name: union.discriminant.name,
            wasExplicitlySet: true
        };
    }
    return {
        name: getUnionDiscriminant(union),
        wasExplicitlySet: false
    };
}

export function getSingleUnionTypeName({
    unionKey,
    rawSingleUnionType
}: {
    unionKey: string;
    rawSingleUnionType: string | RawSchemas.SingleUnionTypeSchema;
}): {
    name: string;
    wasExplicitlySet: boolean;
} {
    if (typeof rawSingleUnionType !== "string" && rawSingleUnionType.name != null) {
        return {
            name: rawSingleUnionType.name,
            wasExplicitlySet: true
        };
    }

    return {
        name: unionKey,
        wasExplicitlySet: false
    };
}

export function getSingleUnionTypeProperties({
    rawSingleUnionType,
    rawValueType,
    parsedValueType,
    file,
    typeResolver
}: {
    rawSingleUnionType: RawSchemas.SingleUnionTypeSchema;
    rawValueType: string;
    parsedValueType: TypeReference;
    file: FernFileContext;
    typeResolver: TypeResolver;
}): SingleUnionTypeProperties.SamePropertiesAsObject | SingleUnionTypeProperties.SingleProperty {
    const singlePropertyKey = typeof rawSingleUnionType !== "string" ? rawSingleUnionType.key : undefined;

    const resolvedType = typeResolver.resolveTypeOrThrow({ type: rawValueType, file });

    // Check if the type is wrapped in optional/nullable - if so, we should NOT use samePropertiesAsObject
    // because the value can be undefined/null and needs to be wrapped in a value property
    const isOptionalOrNullable = isWrappedInOptionalOrNullable(resolvedType);

    // Unwrap nullable/optional containers to check the underlying type
    const unwrappedType = unwrapNullableAndOptional(resolvedType);
    if (
        !isOptionalOrNullable &&
        unwrappedType._type === "named" &&
        isRawObjectDefinition(unwrappedType.declaration) &&
        singlePropertyKey == null
    ) {
        return SingleUnionTypeProperties.samePropertiesAsObject(unwrappedType.name);
    }
    return SingleUnionTypeProperties.singleProperty({
        name: file.casingsGenerator.generateNameAndWireValue({
            wireValue: getSinglePropertyKeyValue(singlePropertyKey),
            name: getSinglePropertyKeyName(singlePropertyKey)
        }),
        type: parsedValueType
    });
}

function getSinglePropertyKeyName(
    rawSingleUnionType: string | RawSchemas.SingleUnionTypeKeySchema | undefined
): string {
    if (rawSingleUnionType != null) {
        if (typeof rawSingleUnionType === "string") {
            return rawSingleUnionType;
        }
        return rawSingleUnionType.name ?? rawSingleUnionType.value;
    }
    return DEFAULT_UNION_VALUE_PROPERTY_VALUE;
}

function getSinglePropertyKeyValue(
    rawSingleUnionType: string | RawSchemas.SingleUnionTypeKeySchema | undefined
): string {
    if (rawSingleUnionType != null) {
        if (typeof rawSingleUnionType === "string") {
            return rawSingleUnionType;
        }
        return rawSingleUnionType.value;
    }
    return DEFAULT_UNION_VALUE_PROPERTY_VALUE;
}

/**
 * Checks if the type is wrapped in optional or nullable containers.
 * When a union variant type is optional/nullable, we should NOT use samePropertiesAsObject
 * because the value can be undefined/null and needs proper handling with a value property.
 */
function isWrappedInOptionalOrNullable(resolvedType: ResolvedType): boolean {
    if (resolvedType._type === "container") {
        if (resolvedType.container._type === "nullable" || resolvedType.container._type === "optional") {
            return true;
        }
    }
    return false;
}

/**
 * Unwraps nullable and optional containers to get the underlying type.
 * This is used to check if the underlying type is an object for potential samePropertiesAsObject usage.
 */
function unwrapNullableAndOptional(resolvedType: ResolvedType): ResolvedType {
    if (resolvedType._type === "container") {
        if (resolvedType.container._type === "nullable" || resolvedType.container._type === "optional") {
            return unwrapNullableAndOptional(resolvedType.container.itemType);
        }
    }
    return resolvedType;
}

function getDiscriminatorContext({ union }: { union: RawSchemas.DiscriminatedUnionSchema }): UnionDiscriminatorContext {
    const discriminantContext = typeof union.discriminant === "object" && union.discriminant?.context;

    return discriminantContext === "protocol" ? UnionDiscriminatorContext.Protocol : UnionDiscriminatorContext.Data;
}
