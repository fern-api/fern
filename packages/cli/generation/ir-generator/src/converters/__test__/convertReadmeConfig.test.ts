import { generatorsYml } from "@fern-api/configuration";
import { HttpEndpoint, HttpService, ServiceId } from "@fern-api/ir-sdk";
import { CliError } from "@fern-api/task-context";
import { describe, expect, it } from "vitest";

import { convertReadmeConfig } from "../convertReadmeConfig.js";

describe("convertReadmeConfig", () => {
    it("classifies unmatched readme endpoints as config errors", () => {
        const readme: generatorsYml.ReadmeSchema = {
            defaultEndpoint: "POST /smtp/email"
        };
        const services: Record<ServiceId, HttpService> = {};

        expect(() => convertReadmeConfig({ readme, services })).toThrowError(
            new CliError({
                message: "Endpoint not found in generators.yml readme config for POST /smtp/email",
                code: CliError.Code.ConfigError
            })
        );
    });

    it("resolves readme endpoint objects to matching IR endpoints", () => {
        const endpointId = "endpoint_1";
        const serviceId = "service_1";
        const endpoint: HttpEndpoint = {
            id: endpointId,
            name: {
                originalName: "sendEmail",
                camelCase: {
                    unsafeName: "sendEmail",
                    safeName: "sendEmail"
                },
                pascalCase: {
                    unsafeName: "SendEmail",
                    safeName: "SendEmail"
                },
                snakeCase: {
                    unsafeName: "send_email",
                    safeName: "send_email"
                },
                screamingSnakeCase: {
                    unsafeName: "SEND_EMAIL",
                    safeName: "SEND_EMAIL"
                }
            },
            method: "POST",
            fullPath: {
                head: "/smtp/email",
                parts: []
            },
            path: {
                head: "/email",
                parts: []
            },
            allPathParameters: [],
            pathParameters: [],
            queryParameters: [],
            headers: [],
            responseHeaders: undefined,
            basePath: undefined,
            v2BaseUrls: undefined,
            requestBody: undefined,
            v2RequestBodies: undefined,
            response: undefined,
            v2Responses: undefined,
            errors: [],
            availability: undefined,
            audiences: [],
            transport: undefined,
            auth: true,
            security: undefined,
            docs: undefined,
            baseUrl: undefined,
            idempotent: false,
            pagination: undefined,
            userSpecifiedExamples: [],
            autogeneratedExamples: [],
            sdkRequest: undefined,
            v2Examples: undefined,
            displayName: undefined,
            source: undefined,
            retries: undefined,
            apiPlayground: undefined
        };
        const service: HttpService = {
            name: {
                fernFilepath: {
                    allParts: [],
                    packagePath: [],
                    file: undefined
                }
            },
            basePath: {
                head: "/smtp",
                parts: []
            },
            pathParameters: [],
            headers: [],
            endpoints: [endpoint],
            availability: undefined,
            audiences: [],
            transport: undefined,
            displayName: undefined,
            encoding: undefined
        };
        const readme: generatorsYml.ReadmeSchema = {
            defaultEndpoint: {
                method: "POST",
                path: "/smtp/email"
            }
        };
        const services: Record<ServiceId, HttpService> = {
            [serviceId]: service
        };

        expect(convertReadmeConfig({ readme, services }).defaultEndpoint).toBe(endpointId);
    });
});
