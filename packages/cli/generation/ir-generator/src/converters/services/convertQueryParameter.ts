import { RawSchemas } from "@fern-api/fern-definition-schema";
import { QueryParameter } from "@fern-api/ir-sdk";

import { FernFileContext } from "../../FernFileContext.js";
import { convertDeclaration } from "../convertDeclaration.js";
import { convertDefaultToLiteral } from "./convertDefaultToLiteral.js";

export function convertQueryParameter({
    file,
    queryParameterKey,
    queryParameter
}: {
    file: FernFileContext;
    queryParameterKey: string;
    queryParameter: RawSchemas.HttpQueryParameterSchema;
}): QueryParameter {
    const { name } = getQueryParameterName({ queryParameterKey, queryParameter });
    const clientDefaultValue = typeof queryParameter !== "string" ? queryParameter["client-default"] : undefined;
    const defaultValue = typeof queryParameter !== "string" ? queryParameter.default : undefined;
    // Strip the docs-level `default` from the parsed type reference when the
    // default is an array on an `allow-multiple: true` query parameter so we
    // don't try to stuff the array into the V2 primitive's `default` field
    // (which expects a string/number/etc.). The array default is preserved
    // separately on `defaultValue` for the docs UI.
    const queryParameterForType =
        typeof queryParameter !== "string" &&
        queryParameter["allow-multiple"] === true &&
        Array.isArray(queryParameter.default)
            ? { ...queryParameter, default: undefined }
            : queryParameter;
    const valueType = file.parseTypeReference(queryParameterForType);
    return {
        ...convertDeclaration(queryParameter),
        name: file.casingsGenerator.generateNameAndWireValue({
            wireValue: queryParameterKey,
            name
        }),
        valueType,
        allowMultiple:
            typeof queryParameter !== "string" && queryParameter["allow-multiple"] != null
                ? queryParameter["allow-multiple"]
                : false,
        clientDefault: convertDefaultToLiteral(clientDefaultValue),
        defaultValue,
        v2Examples: {
            userSpecifiedExamples: {},
            autogeneratedExamples: {}
        },
        explode:
            typeof queryParameter !== "string" && queryParameter.explode != null ? queryParameter.explode : undefined
    };
}

export function getQueryParameterName({
    queryParameterKey,
    queryParameter
}: {
    queryParameterKey: string;
    queryParameter: RawSchemas.HttpQueryParameterSchema;
}): { name: string; wasExplicitlySet: boolean } {
    if (typeof queryParameter !== "string") {
        if (queryParameter.name != null) {
            return { name: queryParameter.name, wasExplicitlySet: true };
        }
    }
    return { name: queryParameterKey, wasExplicitlySet: false };
}
