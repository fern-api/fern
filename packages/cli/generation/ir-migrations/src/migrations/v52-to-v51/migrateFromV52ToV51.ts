function convertRequestBody(requestBody: IrVersions.V52.http.HttpRequestBody): IrVersions.V51.http.HttpRequestBody {
    return IrVersions.V52.http.HttpRequestBody._visit<IrVersions.V51.http.HttpRequestBody>(requestBody, {
        inlinedRequestBody: (inlinedRequestBody) =>
            IrVersions.V51.http.HttpRequestBody.inlinedRequestBody({
                ...inlinedRequestBody,
                properties: inlinedRequestBody.properties.map((property) => ({
                    ...property,
                    valueType: convertTypeReference(property.valueType)
                }))
            }),
        reference: (reference) =>
            IrVersions.V51.http.HttpRequestBody.reference({
                ...reference,
                requestBodyType: convertTypeReference(reference.requestBodyType)
            }),
        fileUpload: (fileUpload) =>
            IrVersions.V51.http.HttpRequestBody.fileUpload({
                ...fileUpload,
                properties: fileUpload.properties.map((fileUploadRequestProperty) => {
                    switch (fileUploadRequestProperty.type) {
                        case "bodyProperty":
                            return IrVersions.V51.http.FileUploadRequestProperty.bodyProperty({
                                ...fileUploadRequestProperty,
                                valueType: convertTypeReference(fileUploadRequestProperty.valueType)
                            });
                        case "file":
                            return IrVersions.V51.http.FileUploadRequestProperty.file(
                                convertFileProperty(fileUploadRequestProperty.value)
                            );
                    }
                })
            }),
        bytes: (bytes) => {
            return IrVersions.V51.http.HttpRequestBody.bytes(bytes);
        },
        _other: () => {
            throw new Error("Unknown HttpRequestBody: " + requestBody.type);
        }
    });
}

