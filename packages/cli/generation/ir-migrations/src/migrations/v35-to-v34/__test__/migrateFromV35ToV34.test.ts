import { AbsoluteFilePath, join, RelativeFilePath } from "@fern-api/fs-utils";
import { createMigrationTester } from "../../../__test__/utils/createMigrationTester";
import { V35_TO_V34_MIGRATION } from "../migrateFromV35ToV34";

const runMigration = createMigrationTester(V35_TO_V34_MIGRATION);

describe("migrateFromV35ToV34", () => {
    it("snapshot", async () => {
        const pathToFixture = join(AbsoluteFilePath.of(__dirname), RelativeFilePath.of("./fixtures/simple"));
        const migrated = await runMigration({
            pathToFixture
        });
        expect(migrated).toMatchSnapshot();
    });
});
