/**
 * Tests for the AI example enhancer's `hasUserSpecifiedV2Examples` predicate.
 *
 * Context: FER-10006 ("Prolific Native Examples Overwritten by AI Generated
 * Examples"). The AI enhancer's first line of defence against overwriting
 * human-authored OpenAPI examples is `hasUserSpecifiedV2Examples`, which is
 * supposed to short-circuit the enhancement pipeline whenever an endpoint
 * already has user-specified examples. These tests assert the DESIRED
 * behavior end-to-end: given an OpenAPI spec with native examples on a
 * request body that has a schema, the predicate must return `true` on the
 * resulting FDR endpoint that the AI enhancer is actually called with.
 *
 * These tests guard against the IR-to-FDR conversion dropping the endpoint's
 * `v2Examples` field, which would make the predicate read `undefined` and
 * return `false` no matter what the OpenAPI author put in their spec.
 */
import { FdrAPI as FdrCjsSdk } from "@fern-api/fdr-sdk";
import { AbsoluteFilePath, join, RelativeFilePath } from "@fern-api/fs-utils";
import { OSSWorkspace } from "@fern-api/lazy-fern-workspace";
import { createMockTaskContext } from "@fern-api/task-context";
import { loadAPIWorkspace } from "@fern-api/workspace-loader";
import assert from "assert";
import { describe, expect, it } from "vitest";

import { convertIrToFdrApi } from "../../ir-to-fdr-converter/convertIrToFdrApi.js";
import { hasUserSpecifiedV2Examples } from "../enhanceExamplesWithAI.js";

const FIXTURES_DIR = join(AbsoluteFilePath.of(__dirname), RelativeFilePath.of("fixtures"));

const SNIPPETS_CONFIG = {
    typescriptSdk: undefined,
    pythonSdk: undefined,
    javaSdk: undefined,
    rubySdk: undefined,
    goSdk: undefined,
    csharpSdk: undefined,
    phpSdk: undefined,
    swiftSdk: undefined,
    rustSdk: undefined
};

type EndpointV3Like = Parameters<typeof hasUserSpecifiedV2Examples>[0];

interface EndpointWithV2Examples extends EndpointV3Like {
    v2Examples?: {
        userSpecifiedExamples: Record<string, unknown>;
        autogeneratedExamples: Record<string, unknown>;
    };
    v2Responses?: {
        responses: Array<{
            body?: {
                v2Examples?: {
                    userSpecifiedExamples: Record<string, unknown>;
                    autogeneratedExamples: Record<string, unknown>;
                };
            };
        }>;
    };
}

interface FdrEndpointWithV2Examples extends FdrCjsSdk.api.v1.register.EndpointDefinition {
    v2Examples?: EndpointWithV2Examples["v2Examples"];
}

async function getFdrApiDefinitionForFixture(fixtureName: string): Promise<FdrCjsSdk.api.v1.register.ApiDefinition> {
    const context = createMockTaskContext();
    const workspace = await loadAPIWorkspace({
        absolutePathToWorkspace: join(FIXTURES_DIR, RelativeFilePath.of(fixtureName)),
        context,
        cliVersion: "0.0.0",
        workspaceName: fixtureName
    });

    assert(workspace.didSucceed);

    if (!(workspace.workspace instanceof OSSWorkspace)) {
        throw new Error(
            `Expected OSSWorkspace for fixture ${fixtureName}, got ${workspace.workspace.constructor.name}`
        );
    }

    const ir = await workspace.workspace.getIntermediateRepresentation({
        context,
        audiences: { type: "all" },
        enableUniqueErrorsPerEndpoint: false,
        generateV1Examples: true,
        logWarnings: false
    });

    return convertIrToFdrApi({
        ir,
        snippetsConfig: SNIPPETS_CONFIG,
        playgroundConfig: { oauth: true },
        context
    });
}

function getOnlyEndpoint(
    apiDefinition: FdrCjsSdk.api.v1.register.ApiDefinition
): FdrCjsSdk.api.v1.register.EndpointDefinition {
    const rootEndpoints = apiDefinition.rootPackage.endpoints;
    const subpackageEndpoints = Object.values(apiDefinition.subpackages).flatMap((subpackage) => subpackage.endpoints);
    const allEndpoints = [...rootEndpoints, ...subpackageEndpoints];
    assert.strictEqual(allEndpoints.length, 1);
    const endpoint = allEndpoints[0];
    assert(endpoint != null);
    return endpoint;
}

function convertFdrEndpointToPredicateInput(
    endpoint: FdrCjsSdk.api.v1.register.EndpointDefinition
): EndpointWithV2Examples {
    const endpointWithV2Examples = endpoint as FdrEndpointWithV2Examples;
    return {
        method: String(endpoint.method),
        examples: [],
        v2Examples: endpointWithV2Examples.v2Examples
    };
}

describe("hasUserSpecifiedV2Examples (synthesised endpoints)", () => {
    it("returns true when v2Examples.userSpecifiedExamples has entries", () => {
        const endpoint: EndpointWithV2Examples = {
            method: "POST",
            examples: [],
            v2Examples: {
                userSpecifiedExamples: {
                    products_createProduct_example: {
                        request: { requestBody: { name: "Foo" } }
                    }
                },
                autogeneratedExamples: {}
            }
        };

        expect(hasUserSpecifiedV2Examples(endpoint)).toBe(true);
    });

    it("returns false when v2Examples is missing entirely", () => {
        const endpoint: EndpointWithV2Examples = {
            method: "POST",
            examples: []
        };

        expect(hasUserSpecifiedV2Examples(endpoint)).toBe(false);
    });

    it("returns false when v2Examples.userSpecifiedExamples is empty", () => {
        const endpoint: EndpointWithV2Examples = {
            method: "POST",
            examples: [],
            v2Examples: {
                userSpecifiedExamples: {},
                autogeneratedExamples: {
                    auto_1: { request: { requestBody: { name: "name" } } }
                }
            }
        };

        expect(hasUserSpecifiedV2Examples(endpoint)).toBe(false);
    });

    it("returns true when a response body's v2Examples.userSpecifiedExamples has entries", () => {
        const endpoint: EndpointWithV2Examples = {
            method: "GET",
            examples: [],
            v2Responses: {
                responses: [
                    {
                        body: {
                            v2Examples: {
                                userSpecifiedExamples: {
                                    "200_example": { id: "abc-123" }
                                },
                                autogeneratedExamples: {}
                            }
                        }
                    }
                ]
            }
        };

        expect(hasUserSpecifiedV2Examples(endpoint)).toBe(true);
    });
});

describe("hasUserSpecifiedV2Examples (real FDR endpoints from OpenAPI fixtures)", () => {
    it("detects a native `example` (singular) on a request body WITH a schema", async () => {
        // Scenario from the FER-10006 bug report: a customer authored a
        // human example via the OpenAPI `example` field on a request body
        // whose media type also has a `schema`. The AI enhancer must
        // recognise this as a user-specified example and skip it.
        const apiDefinition = await getFdrApiDefinitionForFixture("native-example-with-schema");
        const endpoint = getOnlyEndpoint(apiDefinition);

        expect(hasUserSpecifiedV2Examples(convertFdrEndpointToPredicateInput(endpoint))).toBe(true);
    }, 90_000);

    it("detects native `examples` (plural) on a request body WITH a schema", async () => {
        // Same as above but the OpenAPI author used the `examples` map
        // form instead of the single `example` field. Both forms are
        // equally human-authored and must be detected.
        const apiDefinition = await getFdrApiDefinitionForFixture("native-examples-with-schema");
        const endpoint = getOnlyEndpoint(apiDefinition);

        expect(hasUserSpecifiedV2Examples(convertFdrEndpointToPredicateInput(endpoint))).toBe(true);
    }, 90_000);
});
