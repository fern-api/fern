import { describe, expect, it } from "vitest";

import { hasUserSpecifiedV2Examples } from "../enhanceExamplesWithAI.js";

// The function operates on an FDR-shaped endpoint object. We cast through
// unknown for tests to avoid exposing the internal EndpointV3 interface.
// biome-ignore lint/suspicious/noExplicitAny: test helper
function asEndpoint(value: any): Parameters<typeof hasUserSpecifiedV2Examples>[0] {
    return value as Parameters<typeof hasUserSpecifiedV2Examples>[0];
}

describe("hasUserSpecifiedV2Examples", () => {
    it("returns false for an endpoint with no v2Examples fields", () => {
        expect(hasUserSpecifiedV2Examples(asEndpoint({ method: "POST", examples: [] }))).toBe(false);
    });

    it("returns false when endpoint-level userSpecifiedExamples is empty", () => {
        expect(
            hasUserSpecifiedV2Examples(
                asEndpoint({
                    method: "POST",
                    examples: [],
                    v2Examples: {
                        userSpecifiedExamples: {},
                        autogeneratedExamples: { example_1: { request: {} } }
                    }
                })
            )
        ).toBe(false);
    });

    it("returns true when endpoint-level userSpecifiedExamples has entries", () => {
        expect(
            hasUserSpecifiedV2Examples(
                asEndpoint({
                    method: "POST",
                    examples: [],
                    v2Examples: {
                        userSpecifiedExamples: { my_example: { request: {} } },
                        autogeneratedExamples: {}
                    }
                })
            )
        ).toBe(true);
    });

    it("returns true when a request body has user-specified examples (schema-backed requestBody case)", () => {
        // This reflects the shape produced when native OpenAPI `examples:` are
        // defined on a request body mediaType that has a `$ref` schema. The
        // examples attach to v2RequestBodies[i].v2Examples.userSpecifiedExamples
        // and do NOT bubble up to endpoint-level v2Examples.
        expect(
            hasUserSpecifiedV2Examples(
                asEndpoint({
                    method: "POST",
                    examples: [],
                    v2Examples: {
                        userSpecifiedExamples: {},
                        autogeneratedExamples: {}
                    },
                    v2RequestBodies: {
                        requestBodies: [
                            {
                                contentType: "application/json",
                                type: "reference",
                                v2Examples: {
                                    userSpecifiedExamples: {
                                        filters_requirements_request: {
                                            filters: [{ filter_id: "favourite_colour" }]
                                        }
                                    },
                                    autogeneratedExamples: {}
                                }
                            }
                        ]
                    }
                })
            )
        ).toBe(true);
    });

    it("returns false when request bodies only have autogenerated examples", () => {
        expect(
            hasUserSpecifiedV2Examples(
                asEndpoint({
                    method: "POST",
                    examples: [],
                    v2RequestBodies: {
                        requestBodies: [
                            {
                                contentType: "application/json",
                                v2Examples: {
                                    userSpecifiedExamples: {},
                                    autogeneratedExamples: { auto_1: { field: "value" } }
                                }
                            }
                        ]
                    }
                })
            )
        ).toBe(false);
    });

    it("returns true when a response body has user-specified examples", () => {
        expect(
            hasUserSpecifiedV2Examples(
                asEndpoint({
                    method: "GET",
                    examples: [],
                    v2Responses: {
                        responses: [
                            {
                                body: {
                                    type: "response",
                                    v2Examples: {
                                        userSpecifiedExamples: { ok: { id: "1" } },
                                        autogeneratedExamples: {}
                                    }
                                }
                            }
                        ]
                    }
                })
            )
        ).toBe(true);
    });

    it("falls through to response check when endpoint v2Examples.userSpecifiedExamples is empty", () => {
        // Regression: the previous implementation returned early (false) whenever
        // endpoint.v2Examples.userSpecifiedExamples was an object, even if empty,
        // which skipped the response-body check.
        expect(
            hasUserSpecifiedV2Examples(
                asEndpoint({
                    method: "GET",
                    examples: [],
                    v2Examples: {
                        userSpecifiedExamples: {},
                        autogeneratedExamples: {}
                    },
                    v2Responses: {
                        responses: [
                            {
                                body: {
                                    v2Examples: {
                                        userSpecifiedExamples: { ok: { id: "1" } },
                                        autogeneratedExamples: {}
                                    }
                                }
                            }
                        ]
                    }
                })
            )
        ).toBe(true);
    });

    it("handles missing/malformed v2Examples without throwing", () => {
        expect(
            hasUserSpecifiedV2Examples(
                asEndpoint({
                    method: "POST",
                    examples: [],
                    v2RequestBodies: { requestBodies: [null, undefined, { contentType: "application/json" }] },
                    v2Responses: { responses: [null, undefined, { body: null }] }
                })
            )
        ).toBe(false);
    });
});
